def plot_three_spectra_per_fixed_value(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
    f_grid_mHz: np.ndarray,
    x_limits: tuple[float, float] = (1e-3, 1e5),
    y_limits: tuple[float, float] = (1e-21, 1e-9),
) -> list[Path]:
    """
    Plot three representative spectra for each fixed scan value and daisy choice.

    Each selected parameter point is shown with the turbulence band:
    - dashed line: epsilon_turb = 0;
    - solid line: epsilon_turb = 1;
    - shaded colored band: interval between both curves.
    """

    saved: list[Path] = []
    output_dir = Path(output_dir)

    for fixed_value in sorted(valid_nucleation_rows(df, spec)[spec.fixed_col].dropna().unique()):
        for include_daisy in [False, True]:
            data = _rows_for_fixed_and_daisy(df, spec, float(fixed_value), include_daisy)
            selected = _selected_three_rows(data, spec.x_col)
            if selected.empty:
                continue

            colors = _three_value_color_map(selected[spec.x_col].to_numpy(dtype=float))
            fig, ax = plt.subplots(figsize=(8.4, 5.2))
            band_label_used = False

            for _, row in selected.iterrows():
                x_val = float(row[spec.x_col])
                color = colors.get(x_val, THREE_CURVE_COLORS["middle"])

                y_eps0 = reconstructed_total_spectrum(row, f_mHz=f_grid_mHz, epsilon_turb=0.0)
                y_eps1 = reconstructed_total_spectrum(row, f_mHz=f_grid_mHz, epsilon_turb=1.0)

                mask0 = np.isfinite(f_grid_mHz) & np.isfinite(y_eps0) & (f_grid_mHz > 0.0) & (y_eps0 > 0.0)
                mask1 = np.isfinite(f_grid_mHz) & np.isfinite(y_eps1) & (f_grid_mHz > 0.0) & (y_eps1 > 0.0)
                mask_band = mask0 & mask1

                if not np.any(mask0) and not np.any(mask1):
                    continue

                if np.any(mask_band):
                    ax.fill_between(
                        f_grid_mHz[mask_band],
                        np.minimum(y_eps0[mask_band], y_eps1[mask_band]),
                        np.maximum(y_eps0[mask_band], y_eps1[mask_band]),
                        color=color,
                        alpha=0.16,
                        linewidth=0.0,
                        label=(r"$0\leq\epsilon_{\rm turb}\leq1$" if not band_label_used else "_nolegend_"),
                    )
                    band_label_used = True

                if np.any(mask0):
                    ax.loglog(
                        f_grid_mHz[mask0],
                        y_eps0[mask0],
                        color=color,
                        lw=2.15,
                        ls="--",
                        label="_nolegend_",
                    )

                if np.any(mask1):
                    ax.loglog(
                        f_grid_mHz[mask1],
                        y_eps1[mask1],
                        color=color,
                        lw=2.45,
                        ls="-",
                        label=rf"{spec.x_label}={_format_for_label(x_val)}",
                    )

                peak_f = _safe_float(row.get("f_total_peak_mHz"))
                peak_o = _safe_float(row.get("omega_total_peak"))
                if np.isfinite(peak_f) and np.isfinite(peak_o) and peak_f > 0 and peak_o > 0:
                    ax.scatter([peak_f], [peak_o], s=34, color=color, edgecolor="black", linewidth=0.45, zorder=5)

            _draw_detector_sensitivities(ax, f_grid_mHz)

            style_handles = [
                Line2D([0], [0], color="black", lw=2.0, ls="--", label=r"$\epsilon_{\rm turb}=0$"),
                Line2D([0], [0], color="black", lw=2.2, ls="-", label=r"$\epsilon_{\rm turb}=1$"),
            ]
            handles, labels = ax.get_legend_handles_labels()
            handles = handles + style_handles
            labels = labels + [handle.get_label() for handle in style_handles]

            ax.set_xlim(*x_limits)
            ax.set_ylim(*y_limits)
            ax.set_xlabel(r"$f$ [mHz]")
            ax.set_ylabel(r"$h^2\Omega_{\rm GW}(f)$")
            ax.set_title(
                rf"{spec.run_label}: {spec.fixed_label}={_format_for_label(float(fixed_value))}, "
                f"{DAISY_STYLES[include_daisy]['label']}"
            )
            ax.grid(True, which="both", alpha=0.25)
            ax.legend(handles, labels, loc="best", fontsize=8)
            fig.tight_layout()

            basename = (
                f"{spec.name}_three_spectra_"
                f"{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}_"
                f"{DAISY_STYLES[include_daisy]['filename']}"
            )
            saved.extend(_save_figure(fig, output_dir, basename))

    return saved


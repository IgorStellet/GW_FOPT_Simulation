def _draw_detector_sensitivities(ax: plt.Axes, f_mHz: np.ndarray) -> None:
    curves = [
        ("LISA", LISA_SENS, SENSITIVITY_COLORS["LISA"]),
        ("DECIGO", DECIGO_SENS, SENSITIVITY_COLORS["DECIGO"]),
        ("BBO", BBO_SENS, SENSITIVITY_COLORS["BBO"]),
    ]
    for name, fn, color in curves:
        if fn is None:
            continue
        try:
            y = np.asarray(fn(f_mHz), dtype=float)
        except Exception as err:
            print(f"[warning] Could not draw {name}: {err}")
            continue
        mask = np.isfinite(f_mHz) & np.isfinite(y) & (f_mHz > 0) & (y > 0)
        if np.any(mask):
            ax.loglog(f_mHz[mask], y[mask], color=color, lw=1.75, alpha=0.95, label=name)


def _finite_peak(f: np.ndarray, y: np.ndarray) -> tuple[float, float]:
    mask = np.isfinite(f) & np.isfinite(y) & (f > 0.0) & (y > 0.0)
    if not np.any(mask):
        return np.nan, np.nan
    ff = f[mask]
    yy = y[mask]
    idx = int(np.argmax(yy))
    return float(ff[idx]), float(yy[idx])


def _relative_difference(new: np.ndarray | float, ref: np.ndarray | float) -> np.ndarray | float:
    new_arr = np.asarray(new, dtype=float)
    ref_arr = np.asarray(ref, dtype=float)
    out = np.full(np.broadcast_shapes(new_arr.shape, ref_arr.shape), np.nan, dtype=float)
    new_b = np.broadcast_to(new_arr, out.shape)
    ref_b = np.broadcast_to(ref_arr, out.shape)
    mask = np.isfinite(new_b) & np.isfinite(ref_b) & (np.abs(ref_b) > 0.0)
    out[mask] = (new_b[mask] - ref_b[mask]) / ref_b[mask]
    if out.ndim == 0:
        return float(out)
    return out


def _three_value_color_map(values: Sequence[float]) -> dict[float, str]:
    vals = sorted({float(v) for v in values if np.isfinite(float(v))})
    if not vals:
        return {}
    if len(vals) == 1:
        return {vals[0]: THREE_CURVE_COLORS["middle"]}
    if len(vals) == 2:
        return {vals[0]: THREE_CURVE_COLORS["small"], vals[1]: THREE_CURVE_COLORS["large"]}
    return {vals[0]: THREE_CURVE_COLORS["small"], vals[1]: THREE_CURVE_COLORS["middle"], vals[-1]: THREE_CURVE_COLORS["large"]}


def _select_min_middle_max(values: Sequence[float]) -> list[float]:
    vals = sorted({float(v) for v in values if np.isfinite(float(v))})
    if len(vals) <= 3:
        return vals
    return [vals[0], vals[len(vals) // 2], vals[-1]]


def _numeric_columns(df: pd.DataFrame, columns: Iterable[str]) -> pd.DataFrame:
    for col in columns:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")
    return df


def _log_padded_limits(
    x: Sequence[float],
    *,
    pad_fraction: float = 0.18,
    fallback: tuple[float, float] = (1e-3, 1e5),
) -> tuple[float, float]:
    """
    Return compact log-scale limits around finite positive values.

    The padding is multiplicative in log space.  This avoids peak-movement
    plots with huge empty regions while still leaving visual breathing room
    around the smallest and largest points.
    """

    arr = np.asarray(list(x), dtype=float)
    arr = arr[np.isfinite(arr) & (arr > 0.0)]

    if arr.size == 0:
        return fallback

    lo = float(np.nanmin(arr))
    hi = float(np.nanmax(arr))

    if not (np.isfinite(lo) and np.isfinite(hi) and lo > 0.0 and hi > 0.0):
        return fallback

    if np.isclose(lo, hi):
        return lo / 2.0, hi * 2.0

    log_lo = np.log10(lo)
    log_hi = np.log10(hi)
    span = log_hi - log_lo
    pad = max(float(pad_fraction) * span, 0.08)

    return 10.0 ** (log_lo - pad), 10.0 ** (log_hi + pad)




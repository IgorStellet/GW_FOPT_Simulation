def _kappa_sound(alpha: float) -> float:
    alpha = max(float(alpha), 0.0)
    if alpha == 0.0:
        return 0.0
    return float(alpha / (0.73 + 0.083 * math.sqrt(alpha) + alpha))


def _sound_wave_shape(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=float)
    return x**3 * (7.0 / (4.0 + 3.0 * x**2)) ** 3.5


def _turbulence_shape(f_Hz: np.ndarray, f_turb_Hz: float, h_star_Hz: float) -> np.ndarray:
    x = np.asarray(f_Hz, dtype=float) / max(float(f_turb_Hz), 1e-300)
    return x**3 / ((1.0 + x) ** (11.0 / 3.0) * (1.0 + 8.0 * np.pi * f_Hz / max(h_star_Hz, 1e-300)))


def _envelope_shape(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=float)
    return 3.8 * x**2.8 / (1.0 + 2.8 * x**3.8)


def _standard_total_spectrum(
    f_mHz: np.ndarray,
    *,
    Tn_GeV: float,
    alpha: float,
    beta_over_H: float,
    g_star: float = 106.75,
    v_w: float = 1.0,
    epsilon_turb: float = 1.0,
    include_collisions: bool = True,
) -> np.ndarray:
    """Standard semi-analytic FOPT GW template, used only for smooth reconstruction."""

    f_mHz = np.asarray(f_mHz, dtype=float)
    f_Hz = 1.0e-3 * f_mHz
    alpha = max(float(alpha), 0.0)
    beta = max(float(beta_over_H), 1.0e-30)
    Tn = max(float(Tn_GeV), 1.0e-30)
    g = max(float(g_star), 1.0e-30)
    vw = max(float(v_w), 1.0e-6)

    h_over_beta = 1.0 / beta
    gfac = (100.0 / g) ** (1.0 / 3.0)
    tfac = (Tn / 100.0) * (g / 100.0) ** (1.0 / 6.0)

    kappa_v = _kappa_sound(alpha)
    strength = alpha / (1.0 + alpha) if alpha >= 0.0 else 0.0

    # Sound waves
    f_sw_Hz = 1.9e-5 * beta * tfac / vw
    omega_sw = (
        2.65e-6
        * h_over_beta
        * (kappa_v * strength) ** 2
        * gfac
        * vw
        * _sound_wave_shape(f_Hz / max(f_sw_Hz, 1e-300))
    )

    # Turbulence.  In the thesis production scans eps=1 is the requested total.
    kappa_turb = float(epsilon_turb) * kappa_v
    f_turb_Hz = 2.7e-5 * beta * tfac / vw
    h_star_Hz = 16.5e-6 * tfac
    omega_turb = (
        3.35e-4
        * h_over_beta
        * (kappa_turb * strength) ** 1.5
        * gfac
        * vw
        * _turbulence_shape(f_Hz, f_turb_Hz, h_star_Hz)
    )

    # Envelope/collision term.  Kept conservative and small unless the scan peak
    # calibration says otherwise.
    if include_collisions:
        kappa_coll = max(1.0 - kappa_v, 0.0) * 0.02
        f_env_Hz = 16.5e-6 * (0.62 / (1.8 - 0.1 * vw + vw**2)) * beta * tfac
        pref_v = 0.11 * vw**3 / (0.42 + vw**2)
        omega_env = (
            1.67e-5
            * h_over_beta**2
            * (kappa_coll * strength) ** 2
            * gfac
            * pref_v
            * _envelope_shape(f_Hz / max(f_env_Hz, 1e-300))
        )
    else:
        omega_env = np.zeros_like(f_mHz)

    total = omega_sw + omega_turb + omega_env
    return np.asarray(total, dtype=float)


def _empirical_peak_spectrum(f_mHz: np.ndarray, f_peak_mHz: float, omega_peak: float) -> np.ndarray:
    """Safe fallback spectrum with an exact peak at the saved diagnostic peak."""

    f_peak = max(float(f_peak_mHz), 1e-300)
    omega = max(float(omega_peak), 0.0)
    x = np.asarray(f_mHz, dtype=float) / f_peak
    return omega * _sound_wave_shape(x)


def reconstructed_total_spectrum(
    row: Mapping[str, Any],
    *,
    f_mHz: np.ndarray,
    epsilon_turb: float = 1.0,
) -> np.ndarray:
    """
    Reconstruct total eps=1 GW spectrum from scan diagnostics.

    The raw standard template is frequency-shifted and amplitude-rescaled so
    that its maximum coincides with the saved production diagnostics
    f_total_peak_mHz and omega_total_peak whenever those are available.
    """

    Tn = _safe_float(row.get("Tn_GeV"))
    alpha = _safe_float(row.get("alpha"))
    beta = _safe_float(row.get("beta_over_H"))
    target_f = _safe_float(row.get("f_total_peak_mHz"))
    target_o = _safe_float(row.get("omega_total_peak"))

    if not (np.isfinite(Tn) and np.isfinite(alpha) and np.isfinite(beta) and alpha > 0.0 and beta > 0.0):
        if np.isfinite(target_f) and np.isfinite(target_o) and target_f > 0.0 and target_o > 0.0:
            return _empirical_peak_spectrum(f_mHz, target_f, target_o)
        return np.full_like(f_mHz, np.nan, dtype=float)

    raw = _standard_total_spectrum(
        f_mHz,
        Tn_GeV=Tn,
        alpha=alpha,
        beta_over_H=beta,
        epsilon_turb=epsilon_turb,
    )
    raw_peak_f, raw_peak_o = _finite_peak(f_mHz, raw)

    if (
        np.isfinite(target_f)
        and np.isfinite(target_o)
        and target_f > 0.0
        and target_o > 0.0
        and np.isfinite(raw_peak_f)
        and np.isfinite(raw_peak_o)
        and raw_peak_f > 0.0
        and raw_peak_o > 0.0
    ):
        # Evaluate the raw template on a warped grid so the maximum sits at the
        # production peak frequency, then rescale to the production peak height.
        warped_f = np.asarray(f_mHz, dtype=float) * raw_peak_f / target_f
        warped_raw = _standard_total_spectrum(
            warped_f,
            Tn_GeV=Tn,
            alpha=alpha,
            beta_over_H=beta,
            epsilon_turb=epsilon_turb,
        )
        return np.asarray(warped_raw * (target_o / raw_peak_o), dtype=float)

    return raw



def plot_peak_movement(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
) -> list[Path]:
    """Plot the peak motion separately for no-daisy and with-daisy."""

    saved: list[Path] = []
    output_dir = Path(output_dir)
    data_all = valid_nucleation_rows(df, spec)

    for fixed_value in sorted(data_all[spec.fixed_col].dropna().unique()):
        for include_daisy in [False, True]:
            data = data_all[
                np.isclose(data_all[spec.fixed_col], float(fixed_value))
                & (data_all["include_daisy"] == bool(include_daisy))
            ].copy()
            data = data[np.isfinite(data["f_total_peak_mHz"]) & np.isfinite(data["omega_total_peak"])]
            data = data[(data["f_total_peak_mHz"] > 0.0) & (data["omega_total_peak"] > 0.0)]
            data = data.sort_values(spec.x_col)
            if data.empty:
                continue

            fig, ax = plt.subplots(figsize=(7.8, 5.8))
            scan_values = data[spec.x_col].to_numpy(dtype=float)
            norm = plt.Normalize(vmin=float(np.nanmin(scan_values)), vmax=float(np.nanmax(scan_values)))
            cmap = plt.get_cmap("viridis")

            f_peak = data["f_total_peak_mHz"].to_numpy(dtype=float)
            omega_peak = data["omega_total_peak"].to_numpy(dtype=float)
            colors = cmap(norm(scan_values))

            if len(data) >= 2:
                ax.plot(f_peak, omega_peak, color="0.35", lw=0.95, alpha=0.45, ls="-")
            ax.scatter(
                f_peak,
                omega_peak,
                c=colors,
                marker=DAISY_STYLES[include_daisy]["marker"],
                s=52,
                edgecolor="black",
                linewidth=0.35,
                zorder=4,
            )

            ax.set_xscale("log")
            ax.set_yscale("log")
            ax.set_xlim(*_log_padded_limits(f_peak, fallback=(1e-3, 1e5)))
            ax.set_ylim(*_log_padded_limits(omega_peak, fallback=(1e-21, 1e-9)))
            ax.set_xlabel(r"$f_{\rm peak}$ [mHz]")
            ax.set_ylabel(r"$h^2\Omega_{\rm peak}$")
            ax.set_title(
                rf"{spec.run_label}: peak motion, {spec.fixed_label}={_format_for_label(float(fixed_value))}, "
                f"{DAISY_STYLES[include_daisy]['label']}"
            )
            ax.grid(True, which="both", alpha=0.25)

            sm = plt.cm.ScalarMappable(norm=norm, cmap=cmap)
            sm.set_array([])
            cb = fig.colorbar(sm, ax=ax, pad=0.02)
            cb.set_label(spec.x_label)

            fig.tight_layout()
            basename = (
                f"{spec.name}_peak_movement_"
                f"{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}_"
                f"{DAISY_STYLES[include_daisy]['filename']}"
            )
            saved.extend(_save_figure(fig, output_dir, basename))

    return saved


def plot_relative_peak_effect(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
) -> list[Path]:
    """Plot relative daisy effect on f_peak and Omega_peak versus scan parameter."""

    saved: list[Path] = []
    output_dir = Path(output_dir)
    data_all = valid_nucleation_rows(df, spec)
    rows_for_csv: list[dict[str, Any]] = []

    for fixed_value in sorted(data_all[spec.fixed_col].dropna().unique()):
        merged = _matched_daisy_rows(df, spec, float(fixed_value))
        if merged.empty:
            continue

        x = merged[spec.x_col].to_numpy(dtype=float)
        f_no = merged["f_total_peak_mHz_no"].to_numpy(dtype=float)
        f_yes = merged["f_total_peak_mHz_yes"].to_numpy(dtype=float)
        o_no = merged["omega_total_peak_no"].to_numpy(dtype=float)
        o_yes = merged["omega_total_peak_yes"].to_numpy(dtype=float)

        rel_f = np.asarray(_relative_difference(f_yes, f_no), dtype=float)
        rel_o = np.asarray(_relative_difference(o_yes, o_no), dtype=float)

        fig, ax = plt.subplots(figsize=(8.5, 5.2))
        mask_f = np.isfinite(x) & np.isfinite(rel_f)
        mask_o = np.isfinite(x) & np.isfinite(rel_o)

        if np.any(mask_f):
            ax.plot(x[mask_f], rel_f[mask_f], color="#1f77b4", lw=2.35, marker="o", ms=3.7, label=r"$\Delta f_{\rm peak}/f_{\rm peak}$")
        if np.any(mask_o):
            ax.plot(x[mask_o], rel_o[mask_o], color="#9467bd", lw=2.35, marker="s", ms=3.7, label=r"$\Delta\Omega_{\rm peak}/\Omega_{\rm peak}$")

        ax.axhline(0.0, color="black", lw=1.0, ls="--", alpha=0.7)
        ax.set_xlabel(spec.x_label)
        ax.set_ylabel("relative daisy effect")
        ax.set_title(rf"{spec.run_label}: relative peak shift, {spec.fixed_label}={_format_for_label(float(fixed_value))}")
        ax.grid(True, alpha=0.25)
        ax.legend(loc="best", fontsize=8)
        fig.tight_layout()

        basename = f"{spec.name}_relative_peak_shift_{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}"
        saved.extend(_save_figure(fig, output_dir, basename))

        for xi, rfi, roi, f0, f1, o0, o1 in zip(x, rel_f, rel_o, f_no, f_yes, o_no, o_yes):
            rows_for_csv.append(
                {
                    "run": spec.name,
                    spec.fixed_col: float(fixed_value),
                    spec.x_col: float(xi),
                    "f_peak_no_daisy_mHz": f0,
                    "f_peak_with_daisy_mHz": f1,
                    "f_peak_relative_difference": rfi,
                    "omega_peak_no_daisy": o0,
                    "omega_peak_with_daisy": o1,
                    "omega_peak_relative_difference": roi,
                }
            )

    if rows_for_csv:
        pd.DataFrame(rows_for_csv).to_csv(output_dir / f"{spec.name}_relative_peak_shift.csv", index=False)

    return saved



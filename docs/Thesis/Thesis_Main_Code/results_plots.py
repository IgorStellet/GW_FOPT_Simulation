def _rows_for_fixed_and_daisy(df: pd.DataFrame, spec: ScanSpec, fixed_value: float, include_daisy: bool) -> pd.DataFrame:
    data = valid_nucleation_rows(df, spec)
    data = data[(data["include_daisy"] == bool(include_daisy)) & np.isclose(data[spec.fixed_col], float(fixed_value))]
    return data.sort_values(spec.x_col).copy()


def _selected_three_rows(data: pd.DataFrame, x_col: str) -> pd.DataFrame:
    if data.empty:
        return data
    values = _select_min_middle_max(data[x_col].to_numpy(dtype=float))
    rows = []
    for value in values:
        idx = (data[x_col] - value).abs().idxmin()
        rows.append(data.loc[idx])
    out = pd.DataFrame(rows).drop_duplicates(subset=[x_col]).sort_values(x_col)
    return out


def _matched_daisy_rows(df: pd.DataFrame, spec: ScanSpec, fixed_value: float, decimals: int = 8) -> pd.DataFrame:
    data = valid_nucleation_rows(df, spec)
    data = data[np.isclose(data[spec.fixed_col], float(fixed_value))].copy()
    if data.empty:
        return pd.DataFrame()

    data["x_key"] = data[spec.x_col].round(decimals)
    no = data[data["include_daisy"] == False].copy()
    yes = data[data["include_daisy"] == True].copy()
    merged = pd.merge(no, yes, on="x_key", suffixes=("_no", "_yes"), how="inner")
    if merged.empty:
        return merged
    merged[spec.x_col] = merged[f"{spec.x_col}_no"]
    return merged.sort_values(spec.x_col)


def _select_common_rows_for_spectrum_comparison(merged: pd.DataFrame, spec: ScanSpec) -> pd.DataFrame:
    """
    Select three common daisy/no-daisy points:
    - smallest common x;
    - largest common x;
    - common x with the largest relative difference in peak height.
    """

    if merged.empty:
        return merged

    peak_no = merged["omega_total_peak_no"].to_numpy(dtype=float)
    peak_yes = merged["omega_total_peak_yes"].to_numpy(dtype=float)
    rel_peak = np.asarray(_relative_difference(peak_yes, peak_no), dtype=float)
    merged = merged.copy()
    merged["abs_relative_peak_difference"] = np.abs(rel_peak)

    selected_indices: list[int] = []
    x = merged[spec.x_col].to_numpy(dtype=float)
    if x.size:
        selected_indices.append(int(np.nanargmin(x)))
        selected_indices.append(int(np.nanargmax(x)))

    if np.any(np.isfinite(merged["abs_relative_peak_difference"].to_numpy(dtype=float))):
        selected_indices.append(int(np.nanargmax(merged["abs_relative_peak_difference"].to_numpy(dtype=float))))

    selected = merged.iloc[sorted(set(selected_indices))].copy()

    # If the maximum-difference point coincides with an edge, add the middle
    # point so the request always gives up to three comparisons.
    if len(selected) < min(3, len(merged)):
        middle_idx = len(merged) // 2
        selected = pd.concat([selected, merged.iloc[[middle_idx]]], ignore_index=False)
        selected = selected.drop_duplicates(subset=[spec.x_col]).sort_values(spec.x_col)

    return selected.head(3)



def make_all_plots_for_scan(
    run_dir: str | Path,
    spec: ScanSpec,
    *,
    output_dir: str | Path = DEFAULT_OUTPUT_DIR,
    f_min_mHz: float = 1e-3,
    f_max_mHz: float = 1e5,
    n_freq: int = 2400,
) -> dict[str, list[Path]]:
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    df = load_scan_summary(run_dir, spec)
    valid = valid_nucleation_rows(df, spec)
    if valid.empty:
        print(f"[{spec.name}] No valid nucleation rows were found. Skipping.")
        return {}

    # Save the cleaned rows used by the plots.  This is very useful when writing
    # the thesis text because the plotted subset is explicit and reproducible.
    valid.to_csv(output_dir / f"{spec.name}_valid_nucleation_rows.csv", index=False)

    f_grid = np.logspace(np.log10(f_min_mHz), np.log10(f_max_mHz), int(n_freq))

    outputs: dict[str, list[Path]] = {}
    outputs["three_spectra"] = plot_three_spectra_per_fixed_value(
        df,
        spec,
        output_dir=output_dir,
        f_grid_mHz=f_grid,
    )
    outputs["spectrum_daisy_comparisons"] = plot_spectrum_daisy_comparisons(
        df,
        spec,
        output_dir=output_dir,
        f_grid_mHz=f_grid,
    )
    outputs["observables"] = plot_observables_vs_scan_parameter(
        df,
        spec,
        output_dir=output_dir,
    )
    outputs["relative_observables"] = plot_relative_observables(
        df,
        spec,
        output_dir=output_dir,
    )
    outputs["peak_movement"] = plot_peak_movement(
        df,
        spec,
        output_dir=output_dir,
    )
    outputs["relative_peak_effect"] = plot_relative_peak_effect(
        df,
        spec,
        output_dir=output_dir,
    )
    return outputs



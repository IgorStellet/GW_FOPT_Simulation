"""
results_plots.py
================

Final thesis post-processing plots for the scan runs.

This script is intentionally lightweight: it does not recompute phase tracing,
bounces, or nucleation.  It reads the master CSV files generated by
``thesis_gw_results.py`` and creates thesis-ready summary figures.

Input folders expected next to this file
----------------------------------------
    03_Glauber_pure_C_scan/
    05_EFT68_pure_c6_scan/

Output folder
-------------
    results_runs/
        glauber_pure/
        c6c8_pure/

Implemented figures
-------------------
Run 03, pure Gláuber scan:
    1. Total GW spectra for three selected C values at fixed Lambda and daisy choice.
    2. Daisy/no-daisy spectrum comparisons for selected common C values.
    3. alpha, beta/H and (Tc-Tn)/Tc versus C, separated by daisy choice.
    4. Relative daisy effect on those three thermal observables.
    5. Motion of the total GW peak in the (f_peak, Omega_peak) plane, colored by C, separated by daisy choice.
    6. Relative daisy effect on f_peak and Omega_peak.

Run 05, pure EFT68 c6 scan at fixed c8:
    Same set of figures, replacing Lambda -> c8 and C -> c6/f^2.

Important note on spectra
-------------------------
Runs 03 and 05 are scan runs and, in the production code, they usually do not save
G0_gw_spectrum.csv for every point.  Therefore this script reconstructs smooth total
GW spectra from the saved thermal parameters alpha, beta/H and Tn, and then calibrates
the reconstructed curve to the saved peak values ``f_total_peak_mHz`` and
``omega_total_peak``.  Thus the plotted spectra are post-processing reconstructions
anchored to the actual peak diagnostics generated by the production run.
"""


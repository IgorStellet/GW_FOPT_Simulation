def load_scan_summary(run_dir: str | Path, spec: ScanSpec) -> pd.DataFrame:
    path = Path(run_dir) / spec.summary_filename
    if not path.exists():
        raise FileNotFoundError(f"Could not find summary CSV: {path}")
    df = pd.read_csv(path)
    df = _numeric_columns(df, REQUIRED_NUMERIC_COLUMNS)
    if "include_daisy" not in df.columns:
        raise ValueError(f"{path} does not contain include_daisy.")
    df["include_daisy"] = _as_bool_series(df["include_daisy"])
    return df


def valid_nucleation_rows(df: pd.DataFrame, spec: ScanSpec) -> pd.DataFrame:
    """Keep only scan rows that have enough finite information for final plots."""

    required = [spec.fixed_col, spec.x_col, "Tn_GeV", "Tc_GeV", "alpha", "beta_over_H"]
    for col in required:
        if col not in df.columns:
            raise ValueError(f"Missing required column {col!r} in {spec.name} summary.")

    mask = np.ones(len(df), dtype=bool)
    for col in required:
        mask &= np.isfinite(df[col].to_numpy(dtype=float))

    # Prefer actual nucleation points.  The numeric mask is the final authority,
    # because older scans may have slightly different status labels.
    if "status" in df.columns:
        status = df["status"].astype(str)
        mask &= status.str.contains("nucleation", case=False, na=False).to_numpy() | status.str.contains("FOPT", case=False, na=False).to_numpy()

    return df.loc[mask].copy()



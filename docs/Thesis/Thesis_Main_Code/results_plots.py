def _safe_float(value: Any, default: float = np.nan) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return default
    return out if np.isfinite(out) else default


def _as_bool_series(series: pd.Series) -> pd.Series:
    if series.dtype == bool:
        return series
    return series.astype(str).str.strip().str.lower().isin(["true", "1", "yes", "y"])


def _format_for_filename(value: float | int) -> str:
    x = float(value)
    if np.isclose(x, round(x)):
        text = str(int(round(x)))
    else:
        text = f"{x:.6g}"
    return text.replace("-", "m").replace(".", "p")


def _format_for_label(value: float, digits: int = 4) -> str:
    x = float(value)
    if np.isclose(x, round(x)):
        return str(int(round(x)))
    return f"{x:.{digits}g}"


def _save_figure(fig: plt.Figure, output_dir: str | Path, basename: str) -> list[Path]:
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    pdf = out / f"{basename}.pdf"
    png = out / f"{basename}.png"
    fig.savefig(pdf, bbox_inches="tight")
    fig.savefig(png, dpi=300, bbox_inches="tight")
    plt.close(fig)

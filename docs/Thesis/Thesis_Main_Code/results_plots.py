def plot_observables_vs_scan_parameter(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
) -> list[Path]:
    """
    Plot alpha, beta/H and supercooling together, but separated by daisy choice.

    This keeps the joint behavior visible without mixing daisy and no-daisy in
    the same thermal-observable figure.  The daisy comparison is reserved for
    the relative-effect plot.
    """

    saved: list[Path] = []
    output_dir = Path(output_dir)
    data_all = valid_nucleation_rows(df, spec)

    for fixed_value in sorted(data_all[spec.fixed_col].dropna().unique()):
        for include_daisy in [False, True]:
            data = _rows_for_fixed_and_daisy(df, spec, float(fixed_value), include_daisy)
            if data.empty:
                continue

            x = data[spec.x_col].to_numpy(dtype=float)
            alpha = data["alpha"].to_numpy(dtype=float)
            beta = data["beta_over_H"].to_numpy(dtype=float)
            supercooling = data["Tc_minus_Tn_over_Tc"].to_numpy(dtype=float)

            fig, ax_alpha = plt.subplots(figsize=(8.6, 5.35))
            ax_beta = ax_alpha.twinx()
            ax_super = ax_alpha.twinx()
            ax_super.spines["right"].set_position(("axes", 1.15))
            ax_super.spines["right"].set_visible(True)

            mask_alpha = np.isfinite(x) & np.isfinite(alpha)
            mask_beta = np.isfinite(x) & np.isfinite(beta)
            mask_super = np.isfinite(x) & np.isfinite(supercooling)

            if np.any(mask_alpha):
                ax_alpha.plot(
                    x[mask_alpha],
                    alpha[mask_alpha],
                    color=OBSERVABLE_COLORS["alpha"],
                    lw=2.45,
                    marker="o",
                    ms=3.6,
                    label=r"$\alpha$",
                )
            if np.any(mask_beta):
                ax_beta.plot(
                    x[mask_beta],
                    beta[mask_beta],
                    color=OBSERVABLE_COLORS["beta_over_H"],
                    lw=2.45,
                    marker="s",
                    ms=3.4,
                    label=r"$\beta/H_*$",
                )
            if np.any(mask_super):
                ax_super.plot(
                    x[mask_super],
                    supercooling[mask_super],
                    color=OBSERVABLE_COLORS["supercooling"],
                    lw=2.45,
                    marker="^",
                    ms=3.6,
                    label=r"$(T_c-T_n)/T_c$",
                )

            ax_alpha.set_xlabel(spec.x_label)
            ax_alpha.set_ylabel(r"$\alpha$", color=OBSERVABLE_COLORS["alpha"])
            ax_beta.set_ylabel(r"$\beta/H_*$", color=OBSERVABLE_COLORS["beta_over_H"])
            ax_super.set_ylabel(r"$(T_c-T_n)/T_c$", color=OBSERVABLE_COLORS["supercooling"])
            ax_alpha.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["alpha"])
            ax_beta.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["beta_over_H"])
            ax_super.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["supercooling"])

            lines1, labels1 = ax_alpha.get_legend_handles_labels()
            lines2, labels2 = ax_beta.get_legend_handles_labels()
            lines3, labels3 = ax_super.get_legend_handles_labels()
            ax_alpha.legend(lines1 + lines2 + lines3, labels1 + labels2 + labels3, loc="best", fontsize=9)

            ax_alpha.set_title(
                rf"{spec.run_label}: {spec.fixed_label}={_format_for_label(float(fixed_value))}, "
                f"{DAISY_STYLES[include_daisy]['label']}"
            )
            ax_alpha.grid(True, alpha=0.25)
            fig.tight_layout(rect=(0.0, 0.0, 0.86, 1.0))

            basename = (
                f"{spec.name}_observables_"
                f"{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}_"
                f"{DAISY_STYLES[include_daisy]['filename']}"
            )
            saved.extend(_save_figure(fig, output_dir, basename))

    return saved


def plot_relative_observables(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
) -> list[Path]:
    """Plot the relative daisy effect of alpha, beta/H and supercooling together."""

    saved: list[Path] = []
    output_dir = Path(output_dir)
    data_all = valid_nucleation_rows(df, spec)

    observables = [
        ("alpha", r"$\Delta\alpha/\alpha$", OBSERVABLE_COLORS["alpha"], "o"),
        ("beta_over_H", r"$\Delta(\beta/H_*)/(\beta/H_*)$", OBSERVABLE_COLORS["beta_over_H"], "s"),
        ("Tc_minus_Tn_over_Tc", r"$\Delta[(T_c-T_n)/T_c]/[(T_c-T_n)/T_c]$", OBSERVABLE_COLORS["supercooling"], "^"),
    ]

    rows_for_csv: list[dict[str, Any]] = []

    for fixed_value in sorted(data_all[spec.fixed_col].dropna().unique()):
        merged = _matched_daisy_rows(df, spec, float(fixed_value))
        if merged.empty:
            continue

        fig, ax = plt.subplots(figsize=(8.5, 5.2))
        x = merged[spec.x_col].to_numpy(dtype=float)

        for col, label, color, marker in observables:
            y_no = merged[f"{col}_no"].to_numpy(dtype=float)
            y_yes = merged[f"{col}_yes"].to_numpy(dtype=float)
            rel = np.asarray(_relative_difference(y_yes, y_no), dtype=float)
            mask = np.isfinite(x) & np.isfinite(rel)
            if np.any(mask):
                ax.plot(x[mask], rel[mask], color=color, lw=2.35, marker=marker, ms=3.7, label=label)

            for xi, yi_no, yi_yes, ri in zip(x, y_no, y_yes, rel):
                rows_for_csv.append(
                    {
                        "run": spec.name,
                        spec.fixed_col: float(fixed_value),
                        spec.x_col: float(xi),
                        "observable": col,
                        "no_daisy": yi_no,
                        "with_daisy": yi_yes,
                        "relative_difference": ri,
                    }
                )

        ax.axhline(0.0, color="black", lw=1.0, ls="--", alpha=0.7)
        ax.set_xlabel(spec.x_label)
        ax.set_ylabel("relative daisy effect")
        ax.set_title(rf"{spec.run_label}: relative daisy effect, {spec.fixed_label}={_format_for_label(float(fixed_value))}")
        ax.grid(True, alpha=0.25)
        ax.legend(loc="best", fontsize=8)
        fig.tight_layout()

        basename = f"{spec.name}_relative_observables_{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}"
        saved.extend(_save_figure(fig, output_dir, basename))

    if rows_for_csv:
        pd.DataFrame(rows_for_csv).to_csv(output_dir / f"{spec.name}_relative_observables.csv", index=False)

    return saved


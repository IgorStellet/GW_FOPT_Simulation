def _row_from_merged(merged_row: Mapping[str, Any], suffix: str) -> dict[str, Any]:
    out: dict[str, Any] = {}
    for key, value in merged_row.items():
        key_str = str(key)
        if key_str.endswith(suffix):
            out[key_str[: -len(suffix)]] = value
    # Keep unsuffixed helpers when useful.
    for key in ["C", "Lambda_GeV", "c6_over_f2_TeV2", "c8_over_f4_TeV4"]:
        if key in merged_row and key not in out:
            out[key] = merged_row[key]
    return out



def plot_spectrum_daisy_comparisons(
    df: pd.DataFrame,
    spec: ScanSpec,
    *,
    output_dir: str | Path,
    f_grid_mHz: np.ndarray,
    x_limits: tuple[float, float] = (1e-3, 1e5),
    y_limits: tuple[float, float] = (1e-21, 1e-9),
) -> list[Path]:
    """
    Plot selected daisy/no-daisy spectrum comparisons.

    Visual convention:
    - solid blue: with daisy;
    - dashed blue: no daisy;
    - black hatched band: difference between both curves.
    """

    saved: list[Path] = []
    output_dir = Path(output_dir)
    rows_for_log: list[dict[str, Any]] = []

    for fixed_value in sorted(valid_nucleation_rows(df, spec)[spec.fixed_col].dropna().unique()):
        merged = _matched_daisy_rows(df, spec, float(fixed_value))
        selected = _select_common_rows_for_spectrum_comparison(merged, spec)
        if selected.empty:
            continue

        for i, (_, row) in enumerate(selected.iterrows(), start=1):
            x_val = float(row[spec.x_col])
            row_no = _row_from_merged(row, "_no")
            row_yes = _row_from_merged(row, "_yes")

            y_no = reconstructed_total_spectrum(row_no, f_mHz=f_grid_mHz, epsilon_turb=1.0)
            y_yes = reconstructed_total_spectrum(row_yes, f_mHz=f_grid_mHz, epsilon_turb=1.0)
            mask = (
                np.isfinite(f_grid_mHz)
                & np.isfinite(y_no)
                & np.isfinite(y_yes)
                & (f_grid_mHz > 0.0)
                & (y_no > 0.0)
                & (y_yes > 0.0)
            )
            if not np.any(mask):
                continue

            fig, ax = plt.subplots(figsize=(8.4, 5.2))
            ax.loglog(f_grid_mHz[mask], y_yes[mask], color="#1f77b4", lw=2.55, ls="-", label="with daisy")
            ax.loglog(f_grid_mHz[mask], y_no[mask], color="#1f77b4", lw=2.35, ls="--", label="no daisy")
            ax.fill_between(
                f_grid_mHz[mask],
                np.minimum(y_no[mask], y_yes[mask]),
                np.maximum(y_no[mask], y_yes[mask]),
                facecolor="none",
                edgecolor="black",
                hatch="////",
                linewidth=0.0,
                alpha=0.95,
                label="difference band",
            )
            _draw_detector_sensitivities(ax, f_grid_mHz)
            ax.set_xlim(*x_limits)
            ax.set_ylim(*y_limits)
            ax.set_xlabel(r"$f$ [mHz]")
            ax.set_ylabel(r"$h^2\Omega_{\rm GW}(f)$")
            ax.set_title(
                rf"{spec.run_label}: daisy effect, "
                rf"{spec.fixed_label}={_format_for_label(float(fixed_value))}, "
                rf"{spec.x_label}={_format_for_label(x_val)}"
            )
            ax.grid(True, which="both", alpha=0.25)
            ax.legend(loc="best", fontsize=8)
            fig.tight_layout()

            basename = (
                f"{spec.name}_spectrum_daisy_comparison_"
                f"{spec.fixed_file_prefix}{_format_for_filename(float(fixed_value))}_"
                f"{spec.x_file_prefix}{_format_for_filename(x_val)}_sel{i}"
            )
            saved.extend(_save_figure(fig, output_dir, basename))

            rows_for_log.append(
                {
                    "run": spec.name,
                    spec.fixed_col: float(fixed_value),
                    spec.x_col: x_val,
                    "selection_index": i,
                    "omega_peak_no_daisy": _safe_float(row_no.get("omega_total_peak")),
                    "omega_peak_with_daisy": _safe_float(row_yes.get("omega_total_peak")),
                    "relative_peak_difference": _relative_difference(
                        _safe_float(row_yes.get("omega_total_peak")),
                        _safe_float(row_no.get("omega_total_peak")),
                    ),
                }
            )

    if rows_for_log:
        pd.DataFrame(rows_for_log).to_csv(output_dir / f"{spec.name}_selected_spectrum_comparisons.csv", index=False)

    return saved


def example_C2_inverted_potential_path(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    show: bool = False,
) -> Path | None:
    """
    Example C2: inverted potential -V with the bounce path and direction arrows.
    """

    try:
        bounce = _get_bounce(summary)
    except RuntimeError as err:
        print(f"[Example C2] {err}; skipping plot.")
        return None

    inst = bounce["instanton"]
    profile = bounce["profile"]
    Tn = bounce["Tn"]

    _, phi0 = _safe_phi0(inst, profile)
    phi_top = _safe_phi_top(inst)

    phi_grid = build_phi_grid(inst, margin=0.12, n=1600)
    V_grid = np.asarray(inst.V(phi_grid), dtype=float)
    V_false = float(inst.V(inst.phi_metaMin))

    invV = -(V_grid - V_false)

    fig, ax = plt.subplots(figsize=(8.0, 4.8))
    ax.plot(
        phi_grid,
        invV,
        lw=2.35,
        color=BOUNCE_COLORS["curve"],
        label=r"$-[V(\phi,T_n)-V(\phi_f,T_n)]$",
    )

    # Highlight path from release point to false vacuum.
    phi_a, phi_b = sorted([phi0, inst.phi_metaMin])
    mask = (phi_grid >= phi_a) & (phi_grid <= phi_b)
    ax.plot(
        phi_grid[mask],
        invV[mask],
        lw=3.2,
        color=BOUNCE_COLORS["phi0"],
        label=r"bounce path $\phi_0\to\phi_f$",
    )

    # Direction arrows along the path.
    path_points = np.linspace(phi0, inst.phi_metaMin, 5)[1:-1]
    step = 0.045 * abs(inst.phi_absMin - inst.phi_metaMin)
    direction = np.sign(inst.phi_metaMin - phi0)

    for ph in path_points:
        x0 = float(ph)
        x1 = float(ph + direction * step)
        x1 = np.clip(x1, phi_grid.min(), phi_grid.max())
        y0 = -float(inst.V(x0) - V_false)
        y1 = -float(inst.V(x1) - V_false)

        ax.annotate(
            "",
            xy=(x1, y1),
            xytext=(x0, y0),
            arrowprops=dict(
                arrowstyle="-|>",
                lw=1.9,
                color="black",
            ),
        )

    markers = [
        (inst.phi_metaMin, r"$\phi_f$", BOUNCE_COLORS["false"]),
        (inst.phi_absMin, r"$\phi_t$", BOUNCE_COLORS["true"]),
        (inst.phi_bar, r"$\phi_{\rm bar}$", BOUNCE_COLORS["bar"]),
        (phi_top, r"$\phi_{\rm top}$", BOUNCE_COLORS["top"]),
        (phi0, r"$\phi_0$", BOUNCE_COLORS["phi0"]),
    ]

    for x, label, color in markers:
        ax.scatter(
            [x],
            [-float(inst.V(x) - V_false)],
            s=54,
            color=color,
            edgecolor="black",
            linewidth=0.35,
            zorder=5,
            label=label,
        )
        ax.axvline(
            x,
            lw=1.65,
            ls=":",
            color=color,
            alpha=0.82,
            zorder=1,
        )

    ax.axhline(
        0.0,
        color=BOUNCE_COLORS["false"],
        lw=1.1,
        ls="--",
        alpha=0.65,
    )

    ax.set_xlabel(r"$\phi$ [GeV]")
    ax.set_ylabel(r"$-[V(\phi,T_n)-V(\phi_f,T_n)]$ [GeV$^4$]")
    ax.set_title(rf"Example C2: inverted potential and bounce trajectory at $T_n={Tn:.4g}$ GeV")
    ax.grid(True, alpha=0.25)
    ax.legend(loc="best", fontsize=8, ncol=2)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "C2_inverted_potential_path", close=not show)
    if show:
        plt.show()

    return saved_path



def _finite_positive_peak(
    f: np.ndarray,
    y: np.ndarray,
) -> tuple[float, float]:
    """Return (f_peak, y_peak) for a non-negative spectrum; otherwise (nan, nan)."""

    f = np.asarray(f, dtype=float)
    y = np.asarray(y, dtype=float)

    mask = np.isfinite(f) & np.isfinite(y) & (y > 0.0)
    if not np.any(mask):
        return np.nan, np.nan

    idx_local = np.argmax(y[mask])
    f_valid = f[mask]
    y_valid = y[mask]
    return float(f_valid[idx_local]), float(y_valid[idx_local])


def _flatten_mapping(
    mapping: Mapping[str, Any],
    *,
    parent_key: str = "",
    sep: str = ".",
) -> dict[str, Any]:
    """Flatten a nested mapping into a single-level dictionary."""

    items: dict[str, Any] = {}

    for key, value in mapping.items():
        new_key = f"{parent_key}{sep}{key}" if parent_key else str(key)

        if isinstance(value, Mapping):
            items.update(_flatten_mapping(value, parent_key=new_key, sep=sep))
        else:
            if isinstance(value, np.generic):
                value = value.item()
            items[new_key] = value

    return items


def _save_flat_diagnostics_csv(
    diagnostics: Mapping[str, Any],
    output_dir: str | Path,
    *,
    basename: str = "thesis_diagnostics",
) -> Path:
    """Save a flattened diagnostics dictionary as a simple two-column CSV table."""

    flat = _flatten_mapping(diagnostics)
    path = Path(output_dir) / f"{basename}.csv"

    with open(path, "w", newline="", encoding="utf-8") as fobj:
        writer = csv.writer(fobj)
        writer.writerow(["quantity", "value"])
        for key, value in flat.items():
            writer.writerow([key, value])

    return path


def _gw_peak_frequencies_mHz(
    *,
    beta_over_H: float,
    T_star: float,
    g_star: float,
    v_w: float,
) -> dict[str, float]:
    """
    Peak frequencies in mHz.

    Important convention:
    the current CosmoTransitions.gravitational_Waves implementation uses
    the usual numerical prefactors in mHz, even though some docstrings say Hz.
    Therefore the GW plotting layer of this thesis script works in mHz.
    """

    beta_over_H = float(beta_over_H)
    T_star = float(T_star)
    g_star = float(g_star)
    v_w = float(v_w)

    common = beta_over_H * (T_star / 100.0) * (g_star / 100.0) ** (1.0 / 6.0) / v_w

    f_sw_mHz = 1.9e-2 * common
    f_turb_mHz = 2.7e-2 * common

    velocity_factor = 0.62 / (1.8 - 0.1 * v_w + v_w**2)
    f_coll_mHz = 16.5e-3 * velocity_factor * common

    return {
        "sw_mHz": float(f_sw_mHz),
        "turb_mHz": float(f_turb_mHz),
        "coll_mHz": float(f_coll_mHz),
        "sw_Hz": float(1.0e-3 * f_sw_mHz),
        "turb_Hz": float(1.0e-3 * f_turb_mHz),
        "coll_Hz": float(1.0e-3 * f_coll_mHz),
    }


def _existing_S3_at_Tn(summary: Mapping[str, Any], Tn: float) -> float:
    """
    Reuse the bounce action already computed at Tn whenever possible.

    This avoids one unnecessary call to the tunneling solver during the GW stage.
    """

    bounce = summary.get("bounce", None)
    if isinstance(bounce, Mapping):
        S3 = _as_finite_float_or_none(bounce.get("S3", None))
        if S3 is not None:
            return float(S3)

    key_T = summary.get("key_temperatures", {}) or {}
    S3 = _as_finite_float_or_none(key_T.get("S_at_Tn", None))
    if S3 is not None:
        return float(S3)

    S_over_T = _as_finite_float_or_none(key_T.get("S_over_Tn", None))
    if S_over_T is not None and np.isfinite(Tn) and Tn > 0.0:
        return float(S_over_T * Tn)

    return float("nan")


def _linde_rate_from_action(S3: float, T: float) -> float:
    """
    Thermal nucleation rate using the Linde prefactor.

    Gamma(T) ≈ [S3/(2πT)]^(3/2) T^4 exp[-S3/T].
    """

    S3 = float(S3)
    T = float(T)

    if not np.isfinite(S3) or not np.isfinite(T) or T <= 0.0:
        return float("nan")

    prefactor = (S3 / (2.0 * np.pi * T)) ** 1.5 * T**4
    return float(prefactor * np.exp(-S3 / T))


def compute_gw_spectrum_summary(
    summary: dict[str, Any],
    *,
    f_min_mHz: float = 1e-3,
    f_max_mHz: float = 1e5,
    n_freq: int = 2000,
    g_star: float = 106.75,
    v_w: float = 1.0,
    dT_fraction: float = 1e-3,
    include_sw: bool = True,
    include_turb: bool = True,
    include_coll: bool = True,
    epsilon_turb: float | None = 1.0,
    kappa_coll: float | None = None,
    beta_over_H_override: float | None = None,
) -> dict[str, Any]:
    """
    Compute gravitational-wave quantities and spectra.

    Frequency convention
    --------------------
    The GW module is internally consistent with frequencies in mHz in the
    phenomenological fits. Therefore this function builds the frequency grid
    in mHz, passes mHz to the spectrum functions, and stores both mHz and Hz
    versions for diagnostics.

    Computational note
    ------------------
    This does not recompute the full phase transition. It reuses the traced
    phases and Tn already stored in ``summary``. The only expensive step is the
    finite-difference evaluation of beta/H, which requires bounce actions at
    nearby temperatures unless ``beta_over_H_override`` is provided.
    """

    if summary.get("main_transition", None) is None:
        raise RuntimeError("No main first-order transition found. Cannot compute GW spectrum.")

    gw_calc = _build_gw_calculator_from_summary(summary)
    if gw_calc is None:
        raise RuntimeError("Could not build the GravitationalWaveCalculator from the transition summary.")

    key_T = summary.get("key_temperatures", {}) or {}
    T_star = _as_finite_float_or_none(key_T.get("Tn", None))

    if T_star is None:
        main_transition = summary.get("main_transition", {}) or {}
        T_star = _as_finite_float_or_none(main_transition.get("Tnuc", None))

    if T_star is None or T_star <= 0.0:
        raise RuntimeError(f"Invalid GW temperature T_* = {T_star}.")

    # Reuse the already-computed bounce action at Tn whenever possible.
    S3_Tn = _existing_S3_at_Tn(summary, T_star)
    if np.isfinite(S3_Tn):
        try:
            gw_calc._S3_cache[float(T_star)] = float(S3_Tn)
        except Exception:
            pass

    # Strength parameter alpha is cheap: it uses V and dV/dT at the two minima.
    alpha = float(gw_calc.alpha(T_star, g_star))

    if not np.isfinite(alpha) or alpha <= 0.0:
        raise RuntimeError(f"Invalid GW strength alpha = {alpha}.")

    # beta/H is the expensive part because it needs nearby bounce actions.
    Tmin = float(getattr(gw_calc, "_T_min", T_star - 1.0))
    Tmax = float(getattr(gw_calc, "_T_max", T_star + 1.0))
    margin = min(T_star - Tmin, Tmax - T_star)

    if margin <= 0.0:
        dT = max(1e-4, dT_fraction * max(T_star, 1.0))
    else:
        dT_max = 0.001 * margin
        dT_pref = dT_fraction * max(T_star, 1.0)
        dT = max(1e-4, min(dT_pref, dT_max))

    if beta_over_H_override is not None:
        beta_over_H = float(beta_over_H_override)
    else:
        beta_over_H = float(gw_calc.beta_over_H(T_star, dT))

    if not np.isfinite(beta_over_H) or beta_over_H <= 0.0:
        raise RuntimeError(f"Invalid GW beta/H = {beta_over_H}.")

    R_star_H = float(gw_calc.bubble_radius_over_H(beta_over_H, v_w=v_w))

    if np.isfinite(S3_Tn):
        Gamma_val = _linde_rate_from_action(S3_Tn, T_star)
    else:
        Gamma_val = float(gw_calc.nucleation_rate(T_star))

    peak_freqs = _gw_peak_frequencies_mHz(
        beta_over_H=beta_over_H,
        T_star=T_star,
        g_star=g_star,
        v_w=v_w,
    )

    gw_scales = {
        "gw_T_star_GeV": float(T_star),
        "gw_alpha": float(alpha),
        "gw_beta_over_H": float(beta_over_H),
        "gw_R_star_times_H": float(R_star_H),
        "gw_nucleation_rate_GeV4": float(Gamma_val),
        "gw_dT_for_beta_GeV": float(dT),
        "gw_f_sw_peak_mHz": peak_freqs["sw_mHz"],
        "gw_f_turb_peak_mHz": peak_freqs["turb_mHz"],
        "gw_f_coll_peak_mHz": peak_freqs["coll_mHz"],
        "gw_f_sw_peak_Hz": peak_freqs["sw_Hz"],
        "gw_f_turb_peak_Hz": peak_freqs["turb_Hz"],
        "gw_f_coll_peak_Hz": peak_freqs["coll_Hz"],
        "gw_g_star": float(g_star),
        "gw_v_w": float(v_w),
    }

    summary["gw_scales"] = gw_scales

    f_mHz = np.logspace(np.log10(f_min_mHz), np.log10(f_max_mHz), int(n_freq))
    f_Hz = 1.0e-3 * f_mHz

    # Main spectrum: by default epsilon_turb=1 reproduces the old example.
    spectra = gw_calc.omega_total_h2(
        f_mHz,
        alpha=alpha,
        beta_over_H=beta_over_H,
        T_star=T_star,
        g_star=g_star,
        v_w=v_w,
        include_sw=include_sw,
        include_turb=include_turb,
        include_coll=include_coll,
        epsilon_turb=epsilon_turb,
        kappa_coll=kappa_coll,
    )

    # Auxiliary spectra for the total-band plot: no turbulence and maximal turbulence.
    spectra_no_turb = gw_calc.omega_total_h2(
        f_mHz,
        alpha=alpha,
        beta_over_H=beta_over_H,
        T_star=T_star,
        g_star=g_star,
        v_w=v_w,
        include_sw=include_sw,
        include_turb=include_turb,
        include_coll=include_coll,
        epsilon_turb=0.0,
        kappa_coll=kappa_coll,
    )

    spectra_max_turb = gw_calc.omega_total_h2(
        f_mHz,
        alpha=alpha,
        beta_over_H=beta_over_H,
        T_star=T_star,
        g_star=g_star,
        v_w=v_w,
        include_sw=include_sw,
        include_turb=include_turb,
        include_coll=include_coll,
        epsilon_turb=1.0,
        kappa_coll=kappa_coll,
    )

    peaks: dict[str, dict[str, float]] = {}
    for key in ("sw", "turb", "coll", "total"):
        f_peak_mHz, omega_peak = _finite_positive_peak(f_mHz, spectra[key])
        peaks[key] = {
            "f_peak_mHz": f_peak_mHz,
            "f_peak_Hz": 1.0e-3 * f_peak_mHz if np.isfinite(f_peak_mHz) else np.nan,
            "omega_peak": omega_peak,
        }

    f_peak_no_turb_mHz, omega_peak_no_turb = _finite_positive_peak(
        f_mHz,
        spectra_no_turb["total"],
    )
    f_peak_max_turb_mHz, omega_peak_max_turb = _finite_positive_peak(
        f_mHz,
        spectra_max_turb["total"],
    )

    peaks["total_no_turb"] = {
        "f_peak_mHz": f_peak_no_turb_mHz,
        "f_peak_Hz": 1.0e-3 * f_peak_no_turb_mHz if np.isfinite(f_peak_no_turb_mHz) else np.nan,
        "omega_peak": omega_peak_no_turb,
    }
    peaks["total_max_turb"] = {
        "f_peak_mHz": f_peak_max_turb_mHz,
        "f_peak_Hz": 1.0e-3 * f_peak_max_turb_mHz if np.isfinite(f_peak_max_turb_mHz) else np.nan,
        "omega_peak": omega_peak_max_turb,
    }

    summary["omega_sw_peak"] = peaks["sw"]["omega_peak"]
    summary["omega_turb_peak"] = peaks["turb"]["omega_peak"]
    summary["omega_coll_peak"] = peaks["coll"]["omega_peak"]
    summary["omega_tot_peak"] = peaks["total"]["omega_peak"]

    summary["gw"] = {
        "f_mHz": f_mHz,
        "f_Hz": f_Hz,
        "spectra": spectra,
        "spectra_no_turb": spectra_no_turb,
        "spectra_max_turb": spectra_max_turb,
        "peaks": peaks,
        "parameters": {
            "g_star": g_star,
            "v_w": v_w,
            "dT_fraction": dT_fraction,
            "include_sw": include_sw,
            "include_turb": include_turb,
            "include_coll": include_coll,
            "epsilon_turb": epsilon_turb,
            "kappa_coll": kappa_coll,
            "beta_over_H_override": beta_over_H_override,
            "frequency_unit": "mHz",
        },
        "thermodynamics": {
            "T_star_GeV": T_star,
            "alpha": alpha,
            "beta_over_H": beta_over_H,
            "R_star_times_H": R_star_H,
            "Gamma_Tstar_GeV4": Gamma_val,
            "dT_for_beta_GeV": dT,
            "S3_Tn": S3_Tn,
            "S3_over_Tn": S3_Tn / T_star if np.isfinite(S3_Tn) else np.nan,
            "f_sw_peak_mHz": peak_freqs["sw_mHz"],
            "f_turb_peak_mHz": peak_freqs["turb_mHz"],
            "f_coll_peak_mHz": peak_freqs["coll_mHz"],
            "f_sw_peak_Hz": peak_freqs["sw_Hz"],
            "f_turb_peak_Hz": peak_freqs["turb_Hz"],
            "f_coll_peak_Hz": peak_freqs["coll_Hz"],
        },
    }

    print("\n" + "=" * 76)
    print("Example D – gravitational-wave summary")
    print("=" * 76)
    print(f"  T_*                  : {T_star:.8g} GeV")
    print(f"  alpha                : {alpha:.8e}")
    print(f"  beta/H               : {beta_over_H:.8e}")
    print(f"  R_* H                : {R_star_H:.8e}")
    print(f"  frequency unit       : mHz")
    print(f"  f_sw^peak            : {peak_freqs['sw_mHz']:.8e} mHz")
    print(f"  f_turb^peak          : {peak_freqs['turb_mHz']:.8e} mHz")
    print(f"  f_coll^peak          : {peak_freqs['coll_mHz']:.8e} mHz")
    print(f"  Omega_sw^peak        : {peaks['sw']['omega_peak']:.8e}")
    print(f"  Omega_turb^peak      : {peaks['turb']['omega_peak']:.8e}")
    print(f"  Omega_coll^peak      : {peaks['coll']['omega_peak']:.8e}")
    print(f"  Omega_tot^peak       : {peaks['total']['omega_peak']:.8e}")
    print("=" * 76)

    return summary["gw"]


def example_D1_gw_components(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    show: bool = False,
) -> Path | None:
    """
    Example D1: plot the individual GW contributions and their total.

    The x-axis is in mHz, matching the convention used by the GW module.
    Only non-vanishing components are shown.
    """

    gw = summary.get("gw", None)
    if gw is None:
        print("[Example D1] No GW summary available; skipping plot.")
        return None

    f_mHz = np.asarray(gw["f_mHz"], dtype=float)
    spectra = gw["spectra_max_turb"]

    fig, ax = plt.subplots(figsize=(8.0, 5.0))

    color_map = {
        "sw": "#2ca02c",      # green
        "coll": "#d62728",    # red
        "turb": "#f2c300",    # yellow/gold
        "total": BOUNCE_COLORS["phi0"],  # purple
    }
    label_map = {
        "sw": "sound waves",
        "coll": "bubble collisions",
        "turb": "MHD turbulence",
        "total": "total",
    }

    plotted_any = False

    for key in ("sw", "coll", "turb"):
        y = np.asarray(spectra[key], dtype=float)
        if np.any(np.isfinite(y) & (y > 0.0)):
            ax.loglog(
                f_mHz,
                y,
                lw=2.3,
                color=color_map[key],
                label=label_map[key],
            )
            plotted_any = True

    omega_total = np.asarray(spectra["total"], dtype=float)
    if np.any(np.isfinite(omega_total) & (omega_total > 0.0)):
        ax.loglog(
            f_mHz,
            omega_total,
            lw=2.6,
            ls=":",
            color=color_map["total"],
            label=label_map["total"],
        )
        plotted_any = True

    if not plotted_any:
        print("[Example D1] All GW components are zero; skipping plot.")
        plt.close(fig)
        return None

    ax.set_xlabel(r"$f$ [mHz]")
    ax.set_ylabel(r"$h^2\Omega_{\rm GW}(f)$")
    ax.set_title("Example D1: individual gravitational-wave contributions")
    ax.grid(True, which="both", alpha=0.25)
    ax.legend(loc="best", fontsize=9)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "D1_gw_components", close=not show)
    if show:
        plt.show()

    return saved_path

def example_D2_gw_total_with_sensitivities(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    show: bool = False,
) -> Path | None:
    """
    Example D2: total GW spectrum with LISA/DECIGO/BBO sensitivities.

    The x-axis is in mHz. The hatched region shows the uncertainty/range
    associated with the turbulence contribution, from epsilon_turb=0 to
    epsilon_turb=1.
    """

    gw = summary.get("gw", None)
    if gw is None:
        print("[Example D2] No GW summary available; skipping plot.")
        return None

    f_mHz = np.asarray(gw["f_mHz"], dtype=float)

    spectra_no_turb = gw["spectra_no_turb"]
    spectra_max_turb = gw["spectra_max_turb"]

    omega_tot_no_turb = np.asarray(spectra_no_turb["total"], dtype=float)
    omega_tot_max_turb = np.asarray(spectra_max_turb["total"], dtype=float)

    if not np.any(np.isfinite(omega_tot_max_turb) & (omega_tot_max_turb > 0.0)):
        print("[Example D2] Total GW spectrum is zero; skipping plot.")
        return None

    fig, ax = plt.subplots(figsize=(8.2, 5.2))

    # Detector PIS curves. These functions expect f in mHz.
    omega_LISA = lisa_sensitivity_s_pis(f_mHz)
    omega_DECIGO = decigo_sensitivity_s_pis(f_mHz)
    omega_BBO = bbo_sensitivity_s_pis(f_mHz)

    ax.loglog(f_mHz, omega_LISA, color="#d62728", lw=1.8, label="LISA")
    ax.loglog(f_mHz, omega_DECIGO, color="#ff7f0e", lw=1.8, label="DECIGO")
    ax.loglog(f_mHz, omega_BBO, color="#f2c300", lw=1.8, label="BBO")

    # Total spectrum without and with maximal turbulence.
    purple = BOUNCE_COLORS["phi0"]

    if np.any(np.isfinite(omega_tot_no_turb) & (omega_tot_no_turb > 0.0)):
        ax.loglog(
            f_mHz,
            omega_tot_no_turb,
            lw=2.4,
            color=purple,
            label=r"total, $\epsilon_{\rm turb}=0$",
        )

    ax.loglog(
        f_mHz,
        omega_tot_max_turb,
        lw=2.4,
        ls="--",
        color=purple,
        label=r"total, $\epsilon_{\rm turb}=1$",
    )

    # Hatched turbulence band: contribution varying between 0 and 1.
    lower = np.minimum(omega_tot_no_turb, omega_tot_max_turb)
    upper = np.maximum(omega_tot_no_turb, omega_tot_max_turb)
    mask = (
        np.isfinite(lower)
        & np.isfinite(upper)
        & (upper > 0.0)
        & (upper > lower)
    )

    if np.any(mask):
        ax.fill_between(
            f_mHz[mask],
            lower[mask],
            upper[mask],
            facecolor="none",
            edgecolor=purple,
            hatch="////",
            linewidth=0.0,
            alpha=0.9,
            label=r"$0\leq\epsilon_{\rm turb}\leq1$",
        )

    # Mark peak frequencies in mHz.
    gw_scales = summary.get("gw_scales", {}) or {}
    for key in ("gw_f_sw_peak_mHz", "gw_f_turb_peak_mHz", "gw_f_coll_peak_mHz"):
        f_peak = _as_finite_float_or_none(gw_scales.get(key, None))
        if f_peak is not None and f_mHz.min() < f_peak < f_mHz.max():
            ax.axvline(f_peak, linestyle=":", linewidth=1.1, color="0.35", alpha=0.75)

    ax.set_xlabel(r"$f$ [mHz]")
    ax.set_ylabel(r"$h^2\Omega_{\rm GW}(f)$")
    ax.set_title("Example D2: total GW spectrum and detector sensitivities")
    ax.grid(True, which="both", alpha=0.25)
    ax.legend(loc="best", fontsize=9)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "D2_gw_total_with_sensitivities", close=not show)
    if show:
        plt.show()

    return saved_path


def gather_thesis_diagnostics(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    basename: str = "thesis_diagnostics",
    label: str = "thesis_run",
) -> dict[str, Any]:
    """
    Gather and save a complete diagnostics dictionary for the thesis run.

    It combines:
    - model parameters,
    - thermal key scales,
    - bounce diagnostics,
    - GW thermodynamic quantities,
    - peak amplitudes and frequencies.
    """

    diagnostics: dict[str, Any] = {
        "label": label,
        "model_parameters": dict(summary.get("model_parameters", {}) or {}),
        "scan_parameters": dict(summary.get("scan_parameters", {}) or {}),
        "key_temperatures": dict(summary.get("key_temperatures", {}) or {}),
        "gw_scales": dict(summary.get("gw_scales", {}) or {}),
    }

    gw = summary.get("gw", None)
    if gw is not None:
        diagnostics["gw_run_parameters"] = dict(gw.get("parameters", {}) or {})
        diagnostics["gw_thermodynamics"] = dict(gw.get("thermodynamics", {}) or {})
        diagnostics["gw_peaks"] = dict(gw.get("peaks", {}) or {})

    bounce = summary.get("bounce", None)
    if bounce is not None:
        inst = bounce["instanton"]
        profile = bounce["profile"]

        generic_diag = gather_diagnostics(
            inst,
            profile,
            label=label,
            transition_summary=summary,
        )
        diagnostics["bounce_diagnostics"] = generic_diag
        diagnostics["bounce_settings"] = dict(bounce.get("settings", {}) or {})
        diagnostics["S3"] = float(bounce.get("S3", np.nan))
        diagnostics["S3_over_T"] = float(bounce.get("S3_over_T", np.nan))

    # Save nested JSON using the helper from generic_potential.
    save_diagnostics_summary(
        diagnostics,
        output_dir,
        basename=basename,
        fmt="json",
    )

    # Save a flat CSV table for quick inspection and for thesis tables.
    csv_path = _save_flat_diagnostics_csv(
        diagnostics,
        output_dir,
        basename=basename,
    )

    summary["diagnostics"] = diagnostics
    summary["diagnostics_csv"] = csv_path

    print("\n" + "=" * 76)
    print("Diagnostics saved")
    print("=" * 76)
    print(f"  JSON basename        : {basename}.json")
    print(f"  CSV path             : {csv_path}")
    print("=" * 76)

    return diagnostics




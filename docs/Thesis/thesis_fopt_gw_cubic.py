def example_C4_spatial_slice_t0_z0(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    n_grid: int = 420,
    show: bool = False,
) -> Path | None:
    r"""
    Example C4: instantaneous spatial slice of the bubble at t=0, z=0.

    The bounce profile is interpreted as phi(r), and the slice shows
    phi(sqrt(x^2+y^2)).
    """


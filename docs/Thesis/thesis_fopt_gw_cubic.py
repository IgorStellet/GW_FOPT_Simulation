def example_B1_temperature_scales(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    show: bool = False,
    extra_T_margin: float = 10.0,
) -> Path | None:
    """
    Example B1: compact temperature-gradient plot with the main thermal scales.

    The plot range is centered on the extracted relevant temperatures:
    slightly below the smallest one and slightly above the largest one.
    """

    key_T = summary.get("key_temperatures", {}) or {}
    temperatures = _finite_temperature_values(key_T)

    if not temperatures:
        print("[Example B1] No key temperatures available; skipping plot.")
        return None

    T_low = min(temperatures)
    T_high = max(temperatures)

    # Adaptive margin: at least extra_T_margin, but not absurdly tiny if
    # the characteristic temperatures are very close.
    spread = max(T_high - T_low, 1.0)
    margin = max(float(extra_T_margin), 0.15 * spread)

    T_plot_min = max(0.0, T_low - margin)
    T_plot_max = T_high + margin

    fig, ax = plt.subplots(figsize=(7.2, 2.4))

    band = np.tile(np.linspace(0.0, 1.0, 512), (2, 1))
    ax.imshow(
        band,
        extent=(T_plot_min, T_plot_max, 0.0, 1.0),
        origin="lower",
        aspect="auto",
        cmap="plasma",
        alpha=0.7,
        zorder=0,
    )

    _draw_key_temperature_lines(ax, key_T, label=True)

    ax.set_xlim(T_plot_min, T_plot_max)
    ax.set_ylim(0.0, 1.0)
    ax.set_yticks([])
    ax.set_xlabel(r"$T$ [GeV]")
    ax.set_title("Example B1: thermal milestones of the transition")
    ax.legend(loc="upper right", fontsize=9)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "B1_temperature_scales", close=not show)
    if show:
        plt.show()

    return saved_path


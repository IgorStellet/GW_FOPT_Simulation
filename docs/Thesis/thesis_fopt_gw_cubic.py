    delta_x = 0.055 * abs(inst.phi_absMin - inst.phi_metaMin)
    x_arrow = inst.phi_absMin + np.sign(inst.phi_metaMin - inst.phi_absMin) * delta_x

    ax.annotate(
        "",
        xy=(x_arrow, V_true - V_false),
        xytext=(x_arrow, 0.0),
        arrowprops=dict(
            arrowstyle="<->",
            lw=1.45,
            color=BOUNCE_COLORS["true"],
        ),
    )

    y_text = 0.08 * (np.nanmax(V_shift) - np.nanmin(V_shift))
    x_text = x_arrow + 0.018 * abs(inst.phi_absMin - inst.phi_metaMin)

    ax.text(
        x_text,
        y_text,
        r"$\Delta V_{\rm vac}$",
        color=BOUNCE_COLORS["true"],
        ha="left",
        va="bottom",
        fontsize=10,
    )

    ax.set_xlabel(r"$\phi$ [GeV]")
    ax.set_ylabel(r"$V(\phi,T_n)-V(\phi_f,T_n)$ [GeV$^4$]")
    ax.set_title(rf"Example C1: potential geometry at $T_n={Tn:.4g}$ GeV")

def example_C5_three_dimensional_bubble_volume(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    n_points: int = 14000,
    random_seed: int = 12345,
    show: bool = False,
) -> Path | None:
    """
    Example C5: 3D cutaway/volume visualization of the nucleated bubble.

    The points sample the interior of a sphere and are colored by phi(r), giving
    a visual gradient from the bubble core to the exterior.
    """

    try:
        bounce = _get_bounce(summary)
    except RuntimeError as err:
        print(f"[Example C5] {err}; skipping plot.")
        return None

    inst = bounce["instanton"]
    profile = bounce["profile"]
    Tn = bounce["Tn"]

    r, _, _ = _profile_arrays(profile)
    phi_of_r = _phi_interpolator_from_profile(profile)

    Rmax = float(r[-1])
    rng = np.random.default_rng(random_seed)

    # Draw random points in a cube, then keep those inside the sphere.
    xyz = rng.uniform(-Rmax, Rmax, size=(int(3.0 * n_points), 3))
    radius = np.linalg.norm(xyz, axis=1)
    inside = radius <= Rmax

    xyz = xyz[inside][:n_points]
    radius = radius[inside][:n_points]
    phi_values = phi_of_r(radius)

    # Create a mild cutaway to make the inner gradient visible.
    cutaway = ~((xyz[:, 0] > 0.0) & (xyz[:, 1] > 0.0) & (xyz[:, 2] > 0.0))
    xyz = xyz[cutaway]
    radius = radius[cutaway]
    phi_values = phi_values[cutaway]

    fig = plt.figure(figsize=(7.0, 6.2))
    ax = fig.add_subplot(111, projection="3d")

    sc = ax.scatter(
        xyz[:, 0],
        xyz[:, 1],
        xyz[:, 2],
        c=phi_values,
        cmap="viridis",
        s=3.0,
        alpha=0.55,
        linewidths=0,
        vmin=min(inst.phi_absMin, inst.phi_metaMin),
        vmax=max(inst.phi_absMin, inst.phi_metaMin),
    )

    # Coordinate axes.
    lim = Rmax
    ax.plot([-lim, lim], [0, 0], [0, 0], lw=1.0, alpha=0.8)
    ax.plot([0, 0], [-lim, lim], [0, 0], lw=1.0, alpha=0.8)
    ax.plot([0, 0], [0, 0], [-lim, lim], lw=1.0, alpha=0.8)

    # Approximate wall sphere.
    try:
        wall = inst.wallDiagnostics(profile, frac=(0.1, 0.9))
        Rwall = float(wall.r_hi)
        u = np.linspace(0.0, 2.0 * np.pi, 50)
        v = np.linspace(0.0, np.pi, 25)
        XS = Rwall * np.outer(np.cos(u), np.sin(v))
        YS = Rwall * np.outer(np.sin(u), np.sin(v))
        ZS = Rwall * np.outer(np.ones_like(u), np.cos(v))
        ax.plot_wireframe(XS, YS, ZS, linewidth=0.35, alpha=0.25)
    except Exception:
        pass

    ax.set_xlabel(r"$x$ [GeV$^{-1}$]")
    ax.set_ylabel(r"$y$ [GeV$^{-1}$]")
    ax.set_zlabel(r"$z$ [GeV$^{-1}$]")
    ax.set_title(rf"Example C5: 3D bubble profile at $T_n={Tn:.4g}$ GeV")

    ax.set_xlim(-lim, lim)
    ax.set_ylim(-lim, lim)
    ax.set_zlim(-lim, lim)
    ax.set_box_aspect((1, 1, 1))

    cb = fig.colorbar(sc, ax=ax, shrink=0.75, pad=0.08)
    cb.set_label(r"$\phi(r)$ [GeV]")

    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "C5_bubble_volume_3d", close=not show)
    if show:
        plt.show()

    return saved_path



def _format_value_for_path(value: float | int) -> str:
    """Return a compact, filesystem-safe representation of a numerical value."""

    value_float = float(value)
    if np.isclose(value_float, round(value_float)):
        text = str(int(round(value_float)))
    else:
        text = f"{value_float:.6g}"
    return text.replace("-", "m").replace(".", "p")


def build_results_directory_name(
    *,
    C: float,
    D: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    run_tag: str | None = None,
) -> str:
    """
    Build the automatic output directory name for a thesis run.

    When the Gláuber measure is switched off, the directory name uses
    ``C0_Lambda0`` even if non-zero values of ``C`` and ``Lambda`` were passed.
    This makes pure-cubic runs immediately distinguishable from runs where the
    functional measure is active.
    """

    C_label = C if use_glauber_measure else 0.0
    Lambda_label = Lambda if use_glauber_measure else 0.0

    name = (
        "results_gw_"
        f"C{_format_value_for_path(C_label)}_"
        f"Lambda{_format_value_for_path(Lambda_label)}_"
        f"D{_format_value_for_path(D)}_"
        f"mh{_format_value_for_path(m_h)}"
    )

    if run_tag is not None and str(run_tag).strip():
        clean_tag = str(run_tag).strip().replace(" ", "_")
        name = f"{name}_{clean_tag}"

    return name


def make_results_directory(
    *,
    C: float,
    D: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    run_tag: str | None = None,
    base_dir: str | Path = ".",
) -> Path:
    """Create and return the directory where all figures/results are saved."""

    output_dir = Path(base_dir) / build_results_directory_name(
        C=C,
        D=D,
        Lambda=Lambda,
        m_h=m_h,
        use_glauber_measure=use_glauber_measure,
        run_tag=run_tag,
    )
    output_dir.mkdir(parents=True, exist_ok=True)
    return output_dir


def save_figure(
    fig: plt.Figure,
    output_dir: str | Path,
    filename: str,
    *,
    close: bool = True,
) -> Path:
    """Save a Matplotlib figure in PDF format and return the saved path."""

    output_path = Path(output_dir) / f"{filename}.pdf"
    fig.savefig(output_path, bbox_inches="tight")
    output_path = Path(output_dir) / f"{filename}.png"
    fig.savefig(output_path, dpi=300, bbox_inches="tight")
    if close:
        plt.close(fig)
    return output_path


def zero_temperature_deltaV(
    phi: ArrayLike | float,
    *,
    D: float,
    C: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    real_continuation: bool = False,
) -> np.ndarray:
    r"""
    Compute ``Delta V(phi, 0) = V(phi, 0) - V(0, 0)``.

    Thermal and daisy terms are switched off.  The cubic term is kept in the
    call structure, but it vanishes identically at ``T=0``.
    """

    phi_arr = np.asarray(phi, dtype=float)
    V_phi = V_effective_potential(
        phi_arr,
        0.0,
        D=D,
        C=C,
        Lambda=Lambda,
        m_h=m_h,
        use_glauber_measure=use_glauber_measure,
        include_thermal=False,
        include_daisy=False,
        include_cubic=True,
        real_continuation=real_continuation,
    )
    V_origin = V_effective_potential(
        0.0,
        0.0,
        D=D,
        C=C,
        Lambda=Lambda,
        m_h=m_h,
        use_glauber_measure=use_glauber_measure,
        include_thermal=False,
        include_daisy=False,
        include_cubic=True,
        real_continuation=real_continuation,
    )
    return np.asarray(V_phi - V_origin, dtype=float)


def _finite_minimum(phi: np.ndarray, y: np.ndarray) -> tuple[float, float] | None:
    """Return the finite minimum of a sampled curve, if at least one point exists."""

    finite = np.isfinite(y)
    if not np.any(finite):
        return None
    finite_indices = np.flatnonzero(finite)
    local_index = finite_indices[np.argmin(y[finite])]
    return float(phi[local_index]), float(y[local_index])



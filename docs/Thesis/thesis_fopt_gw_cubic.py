def _plot_zero_temperature_potential(
    *,
    phi_max: float,
    filename: str,
    title: str,
    output_dir: str | Path,
    C: float,
    D: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    n_points: int = 1600,
    show: bool = False,
    real_continuation: bool = False,
    include_inset: bool = False,
    inset_phi_max: float = 300.0,
) -> Path:
    """Shared implementation for the A1 and A2 zero-temperature plots."""

    phi = np.linspace(0.0, phi_max, n_points)
    deltaV = zero_temperature_deltaV(
        phi,
        D=D,
        C=C,
        Lambda=Lambda,
        m_h=m_h,
        use_glauber_measure=use_glauber_measure,
        real_continuation=real_continuation,
    )

    fig, ax = plt.subplots(figsize=(7.8, 5.0))

    ax.plot(phi, deltaV, lw=2.2, label=r"$\Delta V(\phi,0)$")
    ax.axhline(0.0, color="black", lw=1.0, ls="--", alpha=0.65)
    ax.axvline(
        DEFAULT_VEV,
        color="black",
        lw=1.0,
        ls=":",
        alpha=0.65,
        label=r"$v=246$ GeV",
    )

    minimum = _finite_minimum(phi, deltaV)
    if minimum is not None:
        phi_min, V_min = minimum
        ax.scatter(
            [phi_min],
            [V_min],
            s=35,
            zorder=5,
            label=rf"sampled min: $\phi\simeq{phi_min:.1f}$ GeV",
        )

    if use_glauber_measure and C > 0.0:
        phi_branch = Lambda / np.sqrt(C)
        if 0.0 < phi_branch < phi_max:
            ax.axvline(
                phi_branch,
                color="black",
                lw=1.0,
                ls="-.",
                alpha=0.55,
                label=rf"measure branch: $\Lambda/\sqrt{{C}}\simeq{phi_branch:.1f}$ GeV",
            )

    if include_inset:
        zoom_mask = phi <= inset_phi_max
        finite_zoom = zoom_mask & np.isfinite(deltaV)

        if np.any(finite_zoom):
            axins = ax.inset_axes([0.08, 0.42, 0.43, 0.43])
            axins.plot(phi[zoom_mask], deltaV[zoom_mask], lw=1.6)
            axins.axhline(0.0, color="black", lw=0.8, ls="--", alpha=0.55)
            axins.axvline(DEFAULT_VEV, color="black", lw=0.8, ls=":", alpha=0.55)

            y_zoom = deltaV[finite_zoom]
            y_min = float(np.nanmin(y_zoom))
            y_max = float(np.nanmax(y_zoom))
            y_pad = 0.08 * (y_max - y_min + 1e-30)

            axins.set_xlim(0.0, inset_phi_max)
            axins.set_ylim(y_min - y_pad, y_max + y_pad)
            axins.grid(True, alpha=0.2)
            axins.tick_params(labelsize=8)

            mark_inset(
                ax,
                axins,
                loc1=2,
                loc2=4,
                fc="none",
                ec="0.45",
                lw=0.9,
                alpha=0.8,
            )

    measure_label = "with Gláuber measure" if use_glauber_measure else "without Gláuber measure"
    ax.set_title(
        f"{title}\n"
        f"{measure_label}, C={C:g}, Λ={Lambda:g} GeV, D={D:g}, mh={m_h:g} GeV"
    )
    ax.set_xlabel(r"$\phi$ [GeV]")
    ax.set_ylabel(r"$\Delta V(\phi,0)=V(\phi,0)-V(0,0)$ [GeV$^4$]")
    ax.set_xlim(0.0, phi_max)
    ax.grid(True, alpha=0.25)
    ax.legend(loc="upper right", fontsize=9)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, filename, close=not show)
    if show:
        plt.show()
    return saved_path


DEFAULT_VEV = 246.0        # GeV
DEFAULT_MH = 125.0         # GeV
DEFAULT_MW = 80.36         # GeV
DEFAULT_MZ = 91.19         # GeV
DEFAULT_MT = 173.1         # GeV

# Physical degeneracies used in the one-loop W, Z, and top contributions.
# Bosons enter with positive sign; fermions enter with the relative minus sign
# already included in the Coleman-Weinberg-like term below.
N_W = 6.0
N_Z = 3.0
N_TOP = 12.0

PI2 = np.pi**2
EIGHT_PI2 = 8.0 * PI2
SIXTY_FOUR_PI2 = 64.0 * PI2



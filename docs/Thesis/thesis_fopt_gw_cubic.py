def thesis_nucleation_criterion(S: float, T: float) -> float:
    r"""
    Default thesis nucleation criterion.

    The transitionFinder convention is that the nucleation temperature is found
    when this function crosses zero.  The default choice is

        S(T) / T - 140 = 0,

    which is the standard phenomenological criterion used for electroweak-scale
    first-order transitions.
    """

    return S / (T + 1e-100) - 140.0


def V_effective_potential_XT(
    X: ArrayLike | float,
    T: ArrayLike | float,
    *,
    D: float,
    C: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    real_continuation: bool = False,
    thermal_approx: str = "exact",
) -> np.ndarray:
    """
    Vector-field wrapper for the scalar thesis potential.

    CosmoTransitions transition-finding routines expect the field argument as
    a vector ``X``.  Since the thesis model is one-dimensional, we identify

        phi = X[..., 0].

    This wrapper keeps the physics inside ``V_effective_potential`` and only
    adapts the input format.
    """

    X_arr = np.asarray(X, dtype=float)

    if X_arr.ndim == 0:
        phi = X_arr
    elif X_arr.ndim == 1:
        phi = X_arr[0] if X_arr.shape[0] == 1 else X_arr
    else:
        phi = X_arr[..., 0]

    return np.asarray(
        V_effective_potential(
            phi,
            T,
            D=D,
            C=C,
            Lambda=Lambda,
            m_h=m_h,
            use_glauber_measure=use_glauber_measure,
            include_zeroT_loops=True,
            include_thermal=True,
            include_daisy=True,
            include_cubic=True,
            real_continuation=real_continuation,
            thermal_approx=thermal_approx,
        ),
        dtype=float,
    )


def build_finite_temperature_problem(
    *,
    C: float,
    D: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    x_eps: float = 1e-3,
    T_eps: float = 1e-2,
    deriv_order: int = 4,
    real_continuation: bool = False,
    thermal_approx: str = "exact",
) -> dict[str, Any]:
    """
    Build the finite-temperature objects required by transitionFinder.

    Returns the offset potential, its field derivatives, and its temperature
    derivatives in the format expected by the modernized CosmoTransitions code.
    """

    def Vtot_XT(X: ArrayLike | float, T: ArrayLike | float) -> np.ndarray:
        return V_effective_potential_XT(
            X,
            T,
            D=D,
            C=C,
            Lambda=Lambda,
            m_h=m_h,
            use_glauber_measure=use_glauber_measure,
            real_continuation=real_continuation,
            thermal_approx=thermal_approx,
        )

    derivs = build_finite_T_derivatives(
        Vtot=Vtot_XT,
        Ndim=1,
        x_eps=x_eps,
        T_eps=T_eps,
        deriv_order=deriv_order,
        X_ref=np.array([0.0]),
    )

    return {
        "V_raw_XT": Vtot_XT,
        "V_XT": derivs.V,
        "dVdphi_XT": derivs.gradV,
        "hessV_XT": derivs.hessV,
        "dVdT_XT": derivs.dV_dT,
        "dgradT_XT": derivs.dgradV_dT,
    }


def _as_finite_float_or_none(value: Any) -> float | None:
    """Convert a value to float if it is finite; otherwise return None."""

    if value is None:
        return None

    try:
        out = float(value)
    except (TypeError, ValueError):
        return None

    if not np.isfinite(out):
        return None

    return out


def _main_transition_objects(summary: Mapping[str, Any]) -> tuple[Any, Any, Any, Any, dict[str, Any]]:
    """
    Extract the main-transition objects.

    Naming convention:
    - false_phase: high-temperature/metastable phase;
    - true_phase: low-temperature/stable phase.
    """

    main_transition = summary.get("main_transition", None)
    if main_transition is None:
        raise RuntimeError("No first-order main transition was found.")

    phases = summary["phases"]
    false_key = main_transition["high_phase"]
    true_key = main_transition["low_phase"]

    false_phase = phases[false_key]
    true_phase = phases[true_key]
    key_temperatures = dict(summary.get("key_temperatures", {}) or {})

    return main_transition, false_phase, true_phase, false_key, true_key, key_temperatures


def _extract_action_information(main_transition: Mapping[str, Any], Tn: float | None) -> tuple[float, float]:
    """Extract S(Tn) and S(Tn)/Tn from a transitionFinder transition dictionary."""

    S_n = np.nan
    S_over_Tn = np.nan

    obs = main_transition.get("obs", None)
    if isinstance(obs, Mapping):
        S_n = _as_finite_float_or_none(obs.get("S", np.nan)) or np.nan
        S_over_Tn = _as_finite_float_or_none(obs.get("S_over_T", np.nan)) or np.nan

    if not np.isfinite(S_n):
        for key in ("action", "S3", "S_E", "S_action"):
            value = _as_finite_float_or_none(main_transition.get(key, None))
            if value is not None:
                S_n = value
                break

    if not np.isfinite(S_over_Tn) and Tn is not None and Tn > 0.0 and np.isfinite(S_n):
        S_over_Tn = S_n / Tn

    return float(S_n), float(S_over_Tn)


def _extract_critical_temperature(
    summary: Mapping[str, Any],
    main_transition: Mapping[str, Any],
) -> float | None:
    """Extract Tc associated with the main transition."""

    crit_trans = main_transition.get("crit_trans", None)
    if isinstance(crit_trans, Mapping):
        Tc = _as_finite_float_or_none(crit_trans.get("Tcrit", None))
        if Tc is not None:
            return Tc

    false_key = main_transition["high_phase"]
    true_key = main_transition["low_phase"]

    for candidate in summary.get("critical_transitions", []):
        if (
            candidate.get("high_phase") == false_key
            and candidate.get("low_phase") == true_key
        ):
            return _as_finite_float_or_none(candidate.get("Tcrit", None))

    return None


def compute_finite_temperature_summary(
    *,
    C: float,
    D: float,
    Lambda: float,
    m_h: float,
    use_glauber_measure: bool,
    T_min: float = 5.0,
    T_max: float = 500.0,
    phi_scan_range: tuple[float, float] | None = None,
    n_phi_scan: int = 800,
    n_T_seeds: int = 2,
    deltaX_target: float = 0.1,
    x_eps: float = 1e-3,
    T_eps: float = 1e-2,
    deriv_order: int = 4,
    phitol: float = 1e-5,
    overlapAngle: float = 45.0,
    nuclCriterion: Callable[[float, float], float] | None = None,
    Tn_Ttol: float = 1e-3,
    Tn_maxiter: int = 100,
    real_continuation: bool = False,
    thermal_approx: str = "exact",
    verbose: bool = True,
) -> dict[str, Any]:
    """
    Compute the finite-temperature phase structure and main transition.

    This is the central finite-temperature routine of the thesis file.  It:

    1. builds finite-temperature derivative wrappers;
    2. traces minima/phases using transitionFinder;
    3. finds critical and nucleation temperatures;
    4. identifies the spinodal temperatures of the true and false phases.
    """

    if phi_scan_range is None:
        phi_scan_range = (0.0, max(300.0, 1.25 * DEFAULT_VEV))

    problem = build_finite_temperature_problem(
        C=C,
        D=D,
        Lambda=Lambda,
        m_h=m_h,
        use_glauber_measure=use_glauber_measure,
        x_eps=x_eps,
        T_eps=T_eps,
        deriv_order=deriv_order,
        real_continuation=real_continuation,
        thermal_approx=thermal_approx,
    )

    nucleation_function = nuclCriterion or thesis_nucleation_criterion

    tunnelFromPhase_args = {
        "phitol": phitol,
        "overlapAngle": overlapAngle,
        "nuclCriterion": nucleation_function,
        "verbose": verbose,
        "fullTunneling_params": None,
    }

    summary = _build_phases_and_transitions(
        problem["V_XT"],
        problem["dVdphi_XT"],
        problem["hessV_XT"],
        problem["dVdT_XT"],
        problem["dgradT_XT"],
        T_min=T_min,
        T_max=T_max,
        phi_range=phi_scan_range,
        n_phi_scan=n_phi_scan,
        n_T_seeds=n_T_seeds,
        deltaX_target=deltaX_target,
        tunnelFromPhase_args=tunnelFromPhase_args,
        nuclCriterion=nucleation_function,
        Tn_Ttol=Tn_Ttol,
        Tn_maxiter=Tn_maxiter,
        verbose=verbose,
    )

    summary.update(problem)
    summary["model_parameters"] = {
        "C": C,
        "D": D,
        "Lambda": Lambda,
        "m_h": m_h,
        "use_glauber_measure": use_glauber_measure,
    }
    summary["scan_parameters"] = {
        "T_min": T_min,
        "T_max": T_max,
        "phi_scan_range": phi_scan_range,
        "n_phi_scan": n_phi_scan,
        "n_T_seeds": n_T_seeds,
        "deltaX_target": deltaX_target,
        "x_eps": x_eps,
        "T_eps": T_eps,
        "deriv_order": deriv_order,
        "phitol": phitol,
        "overlapAngle": overlapAngle,
        "Tn_Ttol": Tn_Ttol,
        "Tn_maxiter": Tn_maxiter,
    }

    main_transition = summary.get("main_transition", None)
    if main_transition is None:
        print("\n[Finite T] No first-order transition found.")
        summary["key_temperatures"] = {}
        return summary

    phases = summary["phases"]
    false_key = main_transition["high_phase"]
    true_key = main_transition["low_phase"]

    false_phase = phases[false_key]
    true_phase = phases[true_key]

    Tn = _as_finite_float_or_none(main_transition.get("Tnuc", None))
    Tc = _extract_critical_temperature(summary, main_transition)

    spin_false = _spinodal_data_for_phase(false_phase, problem["hessV_XT"])
    spin_true = _spinodal_data_for_phase(true_phase, problem["hessV_XT"])

    spinodal_target = Tn if Tn is not None else Tc
    if spinodal_target is None:
        spinodal_target = 0.5 * (
            max(float(false_phase.T[0]), float(true_phase.T[0]))
            + min(float(false_phase.T[-1]), float(true_phase.T[-1]))
        )

    Ts_false = _closest_spinodal_to_T(spinodal_target, spin_false["T_spinodals"])
    Ts_true = _closest_spinodal_to_T(spinodal_target, spin_true["T_spinodals"])

    S_n, S_over_Tn = _extract_action_information(main_transition, Tn)

    summary["spinodal_false_phase"] = spin_false
    summary["spinodal_true_phase"] = spin_true
    summary["key_temperatures"] = {
        "Tn": Tn,
        "Tc": Tc,
        "Ts_false": Ts_false,
        "Ts_true": Ts_true,
        "T_spinodal_false_phase": Ts_false,
        "T_spinodal_true_phase": Ts_true,
        "T_spinodal_high_phase": Ts_false,
        "T_spinodal_low_phase": Ts_true,
        "S_at_Tn": S_n,
        "S_over_Tn": S_over_Tn,
        "false_phase_key": false_key,
        "true_phase_key": true_key,
    }

    print("\n" + "=" * 76)
    print("Finite-temperature transition summary")
    print("=" * 76)
    print(f" False/high-T phase key : {false_key!r}")
    print(f" True/low-T phase key   : {true_key!r}")
    print("")
    print(f" T_n                    : {Tn if Tn is not None else np.nan:10.5g} GeV")
    print(f" T_c                    : {Tc if Tc is not None else np.nan:10.5g} GeV")
    print(f" T_s^false              : {Ts_false if Ts_false is not None else np.nan:10.5g} GeV")
    print(f" T_s^true               : {Ts_true if Ts_true is not None else np.nan:10.5g} GeV")
    if np.isfinite(S_n):
        print(f" S(T_n)                 : {S_n:10.5g}")
    if np.isfinite(S_over_Tn):
        print(f" S(T_n)/T_n             : {S_over_Tn:10.5g}")
    print("=" * 76)

    return summary


def _finite_temperature_values(key_temperatures: Mapping[str, Any]) -> list[float]:
    """Collect all finite relevant temperatures from a key-temperature dict."""

    out: list[float] = []
    for key in ("Tn", "Tc", "Ts_true", "Ts_false"):
        value = _as_finite_float_or_none(key_temperatures.get(key, None))
        if value is not None:
            out.append(value)

    return out


def _deduplicate_temperature_items(
    items: list[tuple[str, str, float | None]],
    *,
    atol: float = 1e-7,
) -> list[tuple[str, str, float]]:
    """Remove missing and numerically duplicated temperatures."""

    clean: list[tuple[str, str, float]] = []

    for key, label, value in items:
        T_value = _as_finite_float_or_none(value)
        if T_value is None:
            continue

        duplicate = False
        for _, _, old_T in clean:
            if abs(T_value - old_T) <= atol:
                duplicate = True
                break

        if not duplicate:
            clean.append((key, label, T_value))

    return clean


def _draw_key_temperature_lines(
    ax: plt.Axes,
    key_temperatures: Mapping[str, Any],
    *,
    label: bool = False,
    alpha: float = 0.85,
) -> None:
    """Draw vertical lines for Tn, Tc, Ts_true and Ts_false."""

    items = _deduplicate_temperature_items(
        [
            ("Tn", r"$T_n$", key_temperatures.get("Tn", None)),
            ("Tc", r"$T_c$", key_temperatures.get("Tc", None)),
            ("Ts_true", r"$T_s^{\rm true}$", key_temperatures.get("Ts_true", None)),
            ("Ts_false", r"$T_s^{\rm false}$", key_temperatures.get("Ts_false", None)),
        ]
    )

    linestyles = {
        "Tn": "-.",
        "Tc": "--",
        "Ts_true": ":",
        "Ts_false": ":",
    }

    for key, marker_label, T_value in items:
        ax.axvline(
            T_value,
            linestyle=linestyles.get(key, ":"),
            linewidth=1.1,
            alpha=alpha,
            label=marker_label if label else None,
        )


def _phase_phi_curve(
    phase: Any,
    *,
    n_T: int,
    T_min: float | None = None,
    T_max: float | None = None,
) -> tuple[np.ndarray, np.ndarray]:
    """Sample phi_min(T) along a traced phase."""

    phase_T_min = float(np.min(phase.T))
    phase_T_max = float(np.max(phase.T))

    lo = phase_T_min if T_min is None else max(float(T_min), phase_T_min)
    hi = phase_T_max if T_max is None else min(float(T_max), phase_T_max)

    if hi <= lo:
        return np.array([], dtype=float), np.array([], dtype=float)

    T_values = np.linspace(lo, hi, int(n_T))
    phi_values = np.array(
        [np.asarray(phase.valAt(float(T)), dtype=float).ravel()[0] for T in T_values],
        dtype=float,
    )

    return T_values, phi_values


def _phase_m2_curve(
    phase: Any,
    hessV_XT: Callable[[ArrayLike, float], np.ndarray],
    *,
    n_T: int,
    T_min: float | None = None,
    T_max: float | None = None,
) -> tuple[np.ndarray, np.ndarray]:
    """Sample the smallest Hessian eigenvalue along a traced phase."""

    T_values, _ = _phase_phi_curve(phase, n_T=n_T, T_min=T_min, T_max=T_max)

    if T_values.size == 0:
        return T_values, np.array([], dtype=float)

    m2_values: list[float] = []

    for T in T_values:
        x = np.asarray(phase.valAt(float(T)), dtype=float)
        H = np.asarray(hessV_XT(x, float(T)), dtype=float)

        if H.ndim == 0:
            m2_values.append(float(H))
        else:
            H = H.reshape(x.size, x.size)
            m2_values.append(float(np.min(np.linalg.eigvalsh(H))))

    return T_values, np.asarray(m2_values, dtype=float)



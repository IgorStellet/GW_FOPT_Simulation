def example_C7_ode_terms_decomposition(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    show: bool = False,
) -> Path | None:
    r"""
    Example C7: decompose the bounce equation of motion along the profile.

    For an O(3) bounce, the radial equation is

        phi''(r) + alpha/r phi'(r) = dV/dphi,

    with alpha=2.

    This plot shows:
    - phi''(r),
    - alpha phi'(r)/r,
    - V'(phi(r)),

    and overlays V(phi(r)) on a secondary axis.
    """

    try:
        bounce = _get_bounce(summary)
    except RuntimeError as err:
        print(f"[Example C7] {err}; skipping plot.")
        return None

    inst = bounce["instanton"]
    profile = bounce["profile"]
    Tn = bounce["Tn"]

    r, phi, dphi = _profile_arrays(profile)
    phi0 = float(phi[0])

    d2phi = deriv14(dphi, r)
    friction = inst.alpha * dphi / np.maximum(r, 1e-30)
    force = np.asarray(inst.dV(phi), dtype=float)

    V_profile = np.asarray(inst.V(phi), dtype=float)
    V_meta = float(inst.V(inst.phi_metaMin))
    V_0 = float(inst.V(phi0))
    dV_drop = V_0 - V_meta

    print("\n" + "=" * 76)
    print("Example C7 – ODE term decomposition along the bounce")
    print("=" * 76)
    print(f"  V(phi_0)              : {V_0:.8e}")
    print(f"  V(phi_false)          : {V_meta:.8e}")
    print(f"  Delta V = V0 - Vfalse : {dV_drop:.8e}")
    print("=" * 76)

    fig, ax = plt.subplots(figsize=(8.8, 5.0))

    ax.plot(r, d2phi, lw=2.0, label=r"$\phi''(r)$")
    ax.plot(r, friction, lw=2.0, label=rf"$\alpha\,\phi'(r)/r$, $\alpha={inst.alpha}$")
    ax.plot(r, force, lw=2.0, label=r"$V'(\phi(r))$")
    ax.axhline(0.0, color="black", lw=0.85, alpha=0.55)

    ax2 = ax.twinx()
    ax2.plot(
        r,
        V_profile,
        lw=1.8,
        ls="--",
        color="0.25",
        label=r"$V(\phi(r))$",
    )

    # Put Delta V marker later in r, where V(phi(r)) is already near its plateau.
    r_mark = 0.88 * float(r[-1])

    y_lo, y_hi = sorted([V_0, V_meta])
    ax2.vlines(
        r_mark,
        y_lo,
        y_hi,
        color=BOUNCE_COLORS["phi0"],
        lw=3.0,
        alpha=0.9,
    )
    ax2.scatter(
        [r_mark, r_mark],
        [V_0, V_meta],
        color=BOUNCE_COLORS["phi0"],
        s=35,
        zorder=5,
    )
    ax2.text(
        r_mark,
        0.5 * (V_0 + V_meta),
        r"$\Delta V$",
        color=BOUNCE_COLORS["phi0"],
        ha="left",
        va="center",
        fontsize=10,
    )

    ax.set_xlabel(r"$r$ [GeV$^{-1}$]")
    ax.set_ylabel(r"ODE terms [GeV$^3$]")
    ax2.set_ylabel(r"$V(\phi(r),T_n)$ [GeV$^4$]")
    ax.set_title(rf"Example C7: bounce equation terms at $T_n={Tn:.4g}$ GeV")

    ax.grid(True, alpha=0.25)

    lines1, labels1 = ax.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax.legend(lines1 + lines2, labels1 + labels2, loc="lower right", fontsize=9)

    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "C7_ode_terms_decomposition", close=not show)
    if show:
        plt.show()

    summary["example_C7"] = {
        "r": r,
        "phi": phi,
        "dphi": dphi,
        "d2phi": d2phi,
        "friction": friction,
        "force": force,
        "V_profile": V_profile,
        "deltaV_drop": dV_drop,
    }

    return saved_path



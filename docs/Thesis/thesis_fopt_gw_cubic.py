def example_B5_free_energy_difference(
    summary: dict[str, Any],
    *,
    output_dir: str | Path,
    n_T_plot: int = 350,
    show: bool = False,
) -> Path | None:
    r"""
    Example B5: plot the free-energy difference between the two phases.

    We compute

        Delta V_min(T) = V_false(phi_false(T),T) - V_true(phi_true(T),T).

    This is useful because:
    - at T_c, Delta V_min should be close to zero;
    - at T_n, Delta V_min should be positive if the false vacuum is metastable;
    - the separation between T_c and T_n visually measures supercooling.
    """

    try:
        _, false_phase, true_phase, _, _, key_T = _main_transition_objects(summary)
    except RuntimeError:
        print("[Example B5] No main transition available; skipping plot.")
        return None

    V_XT = summary["V_XT"]

    Tn = _as_finite_float_or_none(key_T.get("Tn", None))
    Tc = _as_finite_float_or_none(key_T.get("Tc", None))

    Tmin_overlap = max(float(np.min(false_phase.T)), float(np.min(true_phase.T)))
    Tmax_overlap = min(float(np.max(false_phase.T)), float(np.max(true_phase.T)))

    if Tmax_overlap <= Tmin_overlap:
        print("[Example B5] The true and false phases do not overlap in T; skipping plot.")
        return None

    if Tn is not None and Tc is not None:
        T_lo = max(min(Tn, Tc), Tmin_overlap)
        T_hi = min(max(Tn, Tc), Tmax_overlap)
        if T_hi <= T_lo:
            T_lo, T_hi = Tmin_overlap, Tmax_overlap
    else:
        T_lo, T_hi = Tmin_overlap, Tmax_overlap

    T_values = np.linspace(T_lo, T_hi, int(n_T_plot))

    def V_on_phase(phase: Any, T_value: float) -> float:
        x = np.asarray(phase.valAt(float(T_value)), dtype=float)
        return float(np.asarray(V_XT(x, float(T_value)), dtype=float))

    deltaV = np.array(
        [
            V_on_phase(false_phase, T_value) - V_on_phase(true_phase, T_value)
            for T_value in T_values
        ],
        dtype=float,
    )

    deltaV_Tn = np.nan
    deltaV_Tc = np.nan

    if Tn is not None and T_lo <= Tn <= T_hi:
        deltaV_Tn = V_on_phase(false_phase, Tn) - V_on_phase(true_phase, Tn)

    if Tc is not None and T_lo <= Tc <= T_hi:
        deltaV_Tc = V_on_phase(false_phase, Tc) - V_on_phase(true_phase, Tc)

    print("\n" + "=" * 76)
    print("Example B5 – free-energy difference between phases")
    print("=" * 76)
    print(f"  Plot interval          : [{T_lo:.6g}, {T_hi:.6g}] GeV")
    if Tn is not None:
        print(f"  T_n                    : {Tn:.6g} GeV")
    if Tc is not None:
        print(f"  T_c                    : {Tc:.6g} GeV")
    if np.isfinite(deltaV_Tn):
        print(f"  Delta V_min(T_n)       : {deltaV_Tn:.6e} GeV^4")
    if np.isfinite(deltaV_Tc):
        print(f"  Delta V_min(T_c)       : {deltaV_Tc:.6e} GeV^4")
    print("=" * 76)

    fig, ax = plt.subplots(figsize=(7.2, 4.3))
    ax.plot(T_values, deltaV, lw=2.2, label=r"$V_{\rm false}-V_{\rm true}$")
    ax.axhline(0.0, color="black", linestyle="--", linewidth=1.0, alpha=0.75)

    if Tn is not None and T_lo <= Tn <= T_hi:
        ax.axvline(Tn, linestyle="-.", linewidth=1.2, label=rf"$T_n={Tn:.4g}$ GeV")

    if Tc is not None and T_lo <= Tc <= T_hi:
        ax.axvline(Tc, linestyle="--", linewidth=1.2, label=rf"$T_c={Tc:.4g}$ GeV")

    ax.set_xlabel(r"$T$ [GeV]")
    ax.set_ylabel(r"$\Delta V_{\min}(T)$ [GeV$^4$]")
    ax.set_title("Example B5: free-energy difference between the two vacua")
    ax.grid(True, alpha=0.25)
    ax.legend(loc="best", fontsize=9)
    fig.tight_layout()

    saved_path = save_figure(fig, output_dir, "B5_free_energy_difference", close=not show)
    if show:
        plt.show()

    summary["example_B5"] = {
        "T": T_values,
        "deltaV": deltaV,
        "deltaV_Tn": deltaV_Tn,
        "deltaV_Tc": deltaV_Tc,
    }

    return saved_path



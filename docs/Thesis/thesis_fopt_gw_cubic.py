# Effective potential
# -----------------------------------------------------------------------------


def V_effective_potential(
    phi: ArrayLike | float,
    T: ArrayLike | float | None = None,
    *,
    D: float = 0.0,
    C: float = 0.0,
    Lambda: float = 1000.0,
    m_h: float = DEFAULT_MH,
    v: float = DEFAULT_VEV,
    m_w: float = DEFAULT_MW,
    m_z: float = DEFAULT_MZ,
    m_t: float = DEFAULT_MT,
    use_glauber_measure: bool = False,
    include_zeroT_loops: bool = True,
    include_thermal: bool = True,
    include_daisy: bool = True,
    include_cubic: bool = True,
    real_continuation: bool = False,
    thermal_approx: str = "exact",
    return_terms: bool = False,
) -> np.ndarray | dict[str, Any]:
    r"""
    Evaluate the thesis effective potential.

    The potential is organized as

    .. math::

        V_{\rm eff}(\phi,T)
        = V_{\rm tree}(\phi)
        + \Delta V_{\rm CW}(\phi) + V_{\rm CT}(\phi)
        + \Delta V_T(\phi,T)
        + V_{\rm daisy}(\phi,T)
        + \Delta V_{\rm measure}(\phi)
        + \Delta V_{\rm new}(\phi,T),

    with

    .. math::

        \Delta V_{\rm new}(\phi,T) = -D T \phi^3.

    Parameters
    ----------
    phi : array_like or float
        Higgs radial background field, in GeV.
    T : array_like, float, or None, optional
        Temperature, in GeV.  It is required whenever ``include_thermal``,
        ``include_daisy`` or a non-zero cubic deformation is used.
    D : float, optional
        Dimensionless coefficient of the finite-temperature cubic deformation
        ``-D*T*phi**3``.
    C : float, optional
        Gláuber functional-measure parameter.  It is used only if
        ``use_glauber_measure=True``.
    Lambda : float, optional
        EFT cutoff scale of the functional-measure deformation, in GeV.
    m_h : float, optional
        Higgs mass used in the tree-level quartic normalization, in GeV.
    v : float, optional
        Electroweak vacuum expectation value, in GeV.
    m_w, m_z, m_t : float, optional
        W, Z, and top reference masses, in GeV.
    use_glauber_measure : bool, optional
        If ``True``, include the logarithmic functional-measure contribution.
        If ``False``, the potential contains only the usual electroweak pieces
        plus the new cubic deformation.
    include_zeroT_loops : bool, optional
        If ``True``, include the renormalized zero-temperature W/Z/top
        Coleman-Weinberg-like correction with the counterterm convention used
        in the reference example.
    include_thermal : bool, optional
        If ``True``, include one-loop thermal W/Z/top corrections.
    include_daisy : bool, optional
        If ``True``, include the simplified electroweak daisy contribution.
    include_cubic : bool, optional
        If ``True``, include ``-D*T*phi**3``.  Setting ``D=0`` also removes it.
    real_continuation : bool, optional
        If ``True``, evaluate the Gláuber logarithm as ``log(abs(1 - t))``.
        If ``False``, field values beyond the branch point are returned as
        ``nan``, which is safer for physical scans.
    thermal_approx : str, optional
        Approximation passed to ``CosmoTransitions.Jb`` and ``Jf``.
    return_terms : bool, optional
        If ``True``, return a dictionary with each separate contribution.
        Otherwise, return only the total potential.

    Returns
    -------
    ndarray or dict[str, Any]
        Total effective potential in GeV^4, or a dictionary containing each
        term and the total.

    Notes
    -----
    In this first thesis version, varying ``m_h`` changes the tree-level Higgs
    quartic normalization.  The W, Z, and top masses remain independent inputs,
    so later scans can decide whether these should be held fixed or varied in a
    correlated way.
    """

    phi_arr = np.asarray(phi, dtype=float)

    if Lambda <= 0.0:
        raise ValueError("Lambda must be positive.")
    if v <= 0.0:
        raise ValueError("v must be positive.")
    if m_h <= 0.0:
        raise ValueError("m_h must be positive.")

    needs_temperature = include_thermal or include_daisy or (include_cubic and D != 0.0)
    if needs_temperature and T is None:
        raise ValueError(
            "T must be provided when thermal, daisy, or cubic finite-temperature "
            "terms are enabled."
        )

    T_arr = None if T is None else np.asarray(T, dtype=float)
    if T_arr is not None and np.any(T_arr < 0.0):
        raise ValueError("Temperature T must be non-negative.")

    # ------------------------------------------------------------------
    # 1. Tree-level Higgs-like potential
    # ------------------------------------------------------------------
    # V_tree = m_h^2/(8 v^2) (phi^2 - v^2)^2
    V_tree = (m_h**2) / (8.0 * v**2) * (phi_arr**2 - v**2) ** 2

    # ------------------------------------------------------------------
    # 2. Zero-temperature one-loop correction + counterterm convention
    # ------------------------------------------------------------------
    # This follows the compact W/Z/top expression used in the existing
    # Glauber-paper example.  The phi=0 logarithm is evaluated through the
    # limiting behavior phi^4 log(phi^2/v^2) -> 0.
    g_w = m_w / v
    g_z = m_z / v
    g_t = m_t / v

    V_zeroT_loops = np.zeros_like(phi_arr, dtype=float)
    if include_zeroT_loops:
        with np.errstate(divide="ignore", invalid="ignore"):
            log_phi = np.where(phi_arr != 0.0, np.log(phi_arr**2 / v**2), 0.0)

        common_bracket = phi_arr**4 * (log_phi - 1.5) + 2.0 * phi_arr**2 * v**2
        loop_weight = N_W * g_w**4 + N_Z * g_z**4 - N_TOP * g_t**4
        V_zeroT_loops = loop_weight * common_bracket / SIXTY_FOUR_PI2

    # ------------------------------------------------------------------
    # 3. Gláuber functional-measure contribution
    # ------------------------------------------------------------------
    # t = C phi^2 / Lambda^2,    t0 = C v^2 / Lambda^2
    # This term can be switched off to isolate the effect of -D T phi^3.
    V_measure = np.zeros_like(phi_arr, dtype=float)
    if use_glauber_measure:
        t = C * phi_arr**2 / Lambda**2
        t0 = C * v**2 / Lambda**2
        denom = (1.0 - t0) ** 2

        if np.isclose(denom, 0.0):
            raise ValueError(
                "The Gláuber measure denominator (1 - C*v^2/Lambda^2)^2 is too "
                "close to zero. Choose different C, v, or Lambda."
            )

        if real_continuation:
            log_measure = np.log(np.abs(1.0 - t))
        else:
            valid = (t < 1.0) | (C <= 0.0)
            log_measure = np.empty_like(t, dtype=float)
            log_measure[valid] = np.log(1.0 - t[valid])
            log_measure[~valid] = np.nan

        polynomial_measure = ((1.0 - 2.0 * t0) * t + 0.5 * t**2) / denom
        V_measure = -(Lambda**4) * (log_measure + polynomial_measure) / EIGHT_PI2

    # ------------------------------------------------------------------
    # 4. One-loop thermal correction
    # ------------------------------------------------------------------
    # Delta V_T = T^4/(2*pi^2) [n_W J_b(m_W(phi)/T)
    #                           +n_Z J_b(m_Z(phi)/T)
    #                           +n_t J_f(m_t(phi)/T)]
    V_thermal = np.zeros_like(phi_arr, dtype=float)
    if include_thermal:
        if np.any(T_arr <= 0.0):
            raise ValueError("include_thermal=True requires T > 0.")

        abs_phi = np.abs(phi_arr)
        with np.errstate(divide="ignore", invalid="ignore"):
            x_w = g_w * abs_phi / T_arr
            x_z = g_z * abs_phi / T_arr
            x_t = g_t * abs_phi / T_arr

        V_thermal_bosons = (T_arr**4) / (2.0 * PI2) * (
            N_W * Jb(x_w, approx=thermal_approx)
            + N_Z * Jb(x_z, approx=thermal_approx)
        )
        V_thermal_fermions = (T_arr**4) / (2.0 * PI2) * (
            N_TOP * Jf(x_t, approx=thermal_approx)
        )
        V_thermal = V_thermal_bosons + V_thermal_fermions
        V_thermal = np.real_if_close(V_thermal)

    # ------------------------------------------------------------------
    # 5. Daisy / ring resummation
    # ------------------------------------------------------------------
    # Simplified electroweak longitudinal contribution used in the reference
    # example: 3 longitudinal modes compressed into an effective g^2.
    V_daisy = np.zeros_like(phi_arr, dtype=float)
    if include_daisy:
        if np.any(T_arr <= 0.0):
            raise ValueError("include_daisy=True requires T > 0.")

        abs_phi = np.abs(phi_arr)
        g2_eff = 4.0 * (m_w**2 + m_z**2) / (3.0 * v**2)
        mL2_phi = 0.25 * g2_eff * abs_phi**2
        mL2_T = mL2_phi + (11.0 / 6.0) * g2_eff * T_arr**2

        mL_phi = np.sqrt(np.maximum(mL2_phi, 0.0))
        mL_T = np.sqrt(np.maximum(mL2_T, 0.0))

        V_daisy = -(T_arr / (12.0 * np.pi)) * 3.0 * (mL_T**3 - mL_phi**3)
        V_daisy = np.real_if_close(V_daisy)

    # ------------------------------------------------------------------
    # 6. New finite-temperature cubic deformation
    # ------------------------------------------------------------------
    # Delta V_new = -D T phi^3
    V_cubic = np.zeros_like(phi_arr, dtype=float)
    if include_cubic and D != 0.0:
        V_cubic = -D * T_arr * phi_arr**3

    # ------------------------------------------------------------------
    # 7. Total effective potential
    # ------------------------------------------------------------------
    V_total = (
        V_tree
        + V_zeroT_loops
        + V_measure
        + V_thermal
        + V_daisy
        + V_cubic
    )
    V_total = np.real_if_close(V_total)

    if return_terms:
        return {
            "total": V_total,
            "tree": V_tree,
            "zeroT_loops_and_counterterms": V_zeroT_loops,
            "glauber_measure": V_measure,
            "thermal": V_thermal,
            "daisy": V_daisy,
            "cubic_deformation": V_cubic,
            "parameters": {
                "D": D,
                "C": C,
                "Lambda": Lambda,
                "m_h": m_h,
                "v": v,
                "m_w": m_w,
                "m_z": m_z,
                "m_t": m_t,
                "use_glauber_measure": use_glauber_measure,
                "include_zeroT_loops": include_zeroT_loops,
                "include_thermal": include_thermal,
                "include_daisy": include_daisy,
                "include_cubic": include_cubic,
                "real_continuation": real_continuation,
                "thermal_approx": thermal_approx,
            },
        }

    return V_total



def _extract_bounce_inputs_from_summary(
    summary: Mapping[str, Any],
) -> dict[str, float]:
    """
    Extract Tn, phi_false(Tn), and phi_true(Tn) from the transition summary.
    """

    main_transition = summary.get("main_transition", None)
    phases = summary.get("phases", {})
    key_T = summary.get("key_temperatures", {}) or {}

    if main_transition is None:
        raise RuntimeError("Cannot build bounce: no main transition was found.")

    Tn = _as_finite_float_or_none(key_T.get("Tn", None))
    if Tn is None:
        raise RuntimeError(
            "Cannot build bounce: T_n is missing or non-finite. "
            "The nucleation condition was probably not satisfied."
        )

    false_phase = phases[main_transition["high_phase"]]
    true_phase = phases[main_transition["low_phase"]]

    phi_false = float(np.asarray(false_phase.valAt(Tn), dtype=float).ravel()[0])
    phi_true = float(np.asarray(true_phase.valAt(Tn), dtype=float).ravel()[0])

    if not np.isfinite(phi_false) or not np.isfinite(phi_true):
        raise RuntimeError("Cannot build bounce: non-finite field values at T_n.")

    if np.isclose(phi_false, phi_true):
        raise RuntimeError(
            "Cannot build bounce: true and false vacua are numerically identical at T_n."
        )

    return {
        "Tn": float(Tn),
        "phi_false": float(phi_false),
        "phi_true": float(phi_true),
    }


def _make_phi_only_potential_at_Tn(
    summary: Mapping[str, Any],
    *,
    Tn: float,
) -> Callable[[ArrayLike | float], np.ndarray]:
    """
    Build a phi-only potential V(phi,Tn) for SingleFieldInstanton.
    """

    params = summary.get("model_parameters", {}) or {}

    C = float(params.get("C", 0.0))
    D = float(params.get("D", 0.0))
    Lambda = float(params.get("Lambda", 1000.0))
    m_h = float(params.get("m_h", DEFAULT_MH))
    use_glauber_measure = bool(params.get("use_glauber_measure", False))

    def V_phi(phi: ArrayLike | float) -> np.ndarray:
        return np.asarray(
            V_effective_potential(
                phi,
                Tn,
                D=D,
                C=C,
                Lambda=Lambda,
                m_h=m_h,
                use_glauber_measure=use_glauber_measure,
                include_zeroT_loops=True,
                include_thermal=True,
                include_daisy=True,
                include_cubic=True,
                real_continuation=False,
                thermal_approx="exact",
            ),
            dtype=float,
        )

    return V_phi


def build_bounce_solution_at_Tn(
    summary: dict[str, Any],
    *,
    xguess: float | None = None,
    phitol: float = 1e-5,
    thinCutoff: float = 1e-4,
    npoints: int = 800,
    alpha: int = 2,
    phi_eps: float = 1e-3,
) -> dict[str, Any]:
    """
    Solve the O(3) bounce once at T_n and store all reusable objects.

    The high-temperature phase is interpreted as the false/metastable vacuum,
    and the low-temperature phase is interpreted as the true/stable vacuum.
    """

    bounce_inputs = _extract_bounce_inputs_from_summary(summary)
    Tn = bounce_inputs["Tn"]
    phi_false = bounce_inputs["phi_false"]
    phi_true = bounce_inputs["phi_true"]

    V_phi = _make_phi_only_potential_at_Tn(summary, Tn=Tn)

    inst = SingleFieldInstanton(
        phi_absMin=phi_true,
        phi_metaMin=phi_false,
        V=V_phi,
        alpha=alpha,
        phi_eps=phi_eps,
    )

    profile = compute_profile(
        inst,
        xguess=xguess,
        phitol=phitol,
        thinCutoff=thinCutoff,
        npoints=npoints,
    )

    action_breakdown = inst.actionBreakdown(profile)

    S3 = float(action_breakdown.S_total)
    S3_over_T = S3 / Tn

    print("\n" + "=" * 76)
    print("Example C – bounce solution at T_n")
    print("=" * 76)
    print(f"  T_n              : {Tn:.8g} GeV")
    print(f"  phi_false(T_n)   : {phi_false:.8g} GeV")
    print(f"  phi_true(T_n)    : {phi_true:.8g} GeV")
    print(f"  S3               : {S3:.8e}")
    print(f"  S3/T_n           : {S3_over_T:.8e}")
    print("=" * 76)

    bounce = {
        "Tn": Tn,
        "phi_false": phi_false,
        "phi_true": phi_true,
        "V_phi": V_phi,
        "instanton": inst,
        "profile": profile,
        "action_breakdown": action_breakdown,
        "S3": S3,
        "S3_over_T": S3_over_T,
        "settings": {
            "xguess": xguess,
            "phitol": phitol,
            "thinCutoff": thinCutoff,
            "npoints": npoints,
            "alpha": alpha,
            "phi_eps": phi_eps,
        },
    }

    summary["bounce"] = bounce
    return bounce


def _get_bounce(summary: Mapping[str, Any]) -> dict[str, Any]:
    """Return the bounce dictionary if it exists."""

    bounce = summary.get("bounce", None)
    if bounce is None:
        raise RuntimeError("Bounce has not been computed yet.")
    return bounce


def _profile_arrays(profile: Any) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Extract R, Phi and dPhi arrays from a CosmoTransitions profile object."""

    r = np.asarray(profile.R, dtype=float)
    phi = np.asarray(profile.Phi, dtype=float)

    if hasattr(profile, "dPhi"):
        dphi = np.asarray(profile.dPhi, dtype=float)
    else:
        dphi = np.gradient(phi, r, edge_order=2)

    return r, phi, dphi


def _safe_phi_top(inst: SingleFieldInstanton) -> float:
    """Return phi_top from scale diagnostics, falling back to phi_bar."""

    sinfo = getattr(inst, "_scale_info", {}) or {}
    phi_top = sinfo.get("phi_top", None)

    if phi_top is None or not np.isfinite(float(phi_top)):
        phi_top = getattr(inst, "phi_bar", np.nan)

    return float(phi_top)


def _safe_phi0(inst: SingleFieldInstanton, profile: Any) -> tuple[float, float]:
    """Return r0 and phi0 from profile diagnostics, falling back to profile start."""

    r, phi, _ = _profile_arrays(profile)
    pinfo = getattr(inst, "_profile_info", {}) or {}

    r0 = pinfo.get("r0", r[0])
    phi0 = pinfo.get("phi0", phi[0])

    return float(r0), float(phi0)


def _phi_interpolator_from_profile(profile: Any) -> Callable[[ArrayLike | float], np.ndarray]:
    """
    Interpolate phi(r), extending to the true value near the center and to the
    false value outside the last sampled radius.
    """

    r, phi, _ = _profile_arrays(profile)

    return interpolate.interp1d(
        r,
        phi,
        kind="linear",
        bounds_error=False,
        fill_value=(float(phi[0]), float(phi[-1])),
    )



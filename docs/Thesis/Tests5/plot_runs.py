def _valid_fopt_rows(df: pd.DataFrame) -> pd.DataFrame:
    """Keep rows with finite thermodynamic observables."""

    required = ["Lambda_GeV", "C"]
    for col in required:
        if col not in df.columns:
            raise ValueError(f"run03 summary is missing required column {col!r}")

    # Do not require status string because old scans may not use the same text.
    mask = np.isfinite(df["Lambda_GeV"]) & np.isfinite(df["C"])
    return df.loc[mask].copy()


def _relative_run03_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    """
    Pair no-daisy and with-daisy rows and compute relative differences.
    """

    data = _valid_fopt_rows(df).copy()
    data["C_key"] = data["C"].round(8)
    data["Lambda_key"] = data["Lambda_GeV"].round(8)

    no = data[data["include_daisy"] == False].copy()
    yes = data[data["include_daisy"] == True].copy()

    merged = pd.merge(
        no,
        yes,
        on=["Lambda_key", "C_key"],
        suffixes=("_no", "_yes"),
        how="inner",
    )

    out = pd.DataFrame()
    if merged.empty:
        return out

    out["Lambda_GeV"] = merged["Lambda_GeV_no"]
    out["C"] = merged["C_no"]

    for col in ["alpha", "beta_over_H", "Tc_minus_Tn_over_Tc"]:
        no_col = f"{col}_no"
        yes_col = f"{col}_yes"
        out[f"{col}_no"] = merged[no_col]
        out[f"{col}_yes"] = merged[yes_col]

        numerator = merged[yes_col] - merged[no_col]
        denominator = merged[no_col]
        rel = np.full(len(merged), np.nan, dtype=float)
        mask = np.isfinite(numerator) & np.isfinite(denominator) & (np.abs(denominator) > 0.0)
        rel[mask] = numerator[mask] / denominator[mask]
        out[f"{col}_relative"] = rel

    return out


def _plot_run03_observables_one_lambda(
    df: pd.DataFrame,
    *,
    output_dir: str | Path,
    include_daisy: bool,
    Lambda: float,
) -> list[Path]:
    """
    Plot alpha(C), beta/H(C), and (Tc-Tn)/Tc(C) for one fixed Lambda.

    A fixed-Lambda plot is the correct representation here because every Lambda
    scan uses a different C interval.  The three observables have different
    units/scales, so we use three y-axes while keeping a common C axis.
    """

    output_dir = Path(output_dir)
    data = _valid_fopt_rows(df[df["include_daisy"] == include_daisy])
    data = data[np.isclose(data["Lambda_GeV"], float(Lambda))].sort_values("C")

    if data.empty:
        return []

    daisy_text = "with daisy" if include_daisy else "no daisy"
    daisy_file = "with_daisy" if include_daisy else "no_daisy"

    x = data["C"].to_numpy(dtype=float)
    alpha_scaled = 1.0e6 * data["alpha"].to_numpy(dtype=float)
    beta = data["beta_over_H"].to_numpy(dtype=float)
    supercooling = data["Tc_minus_Tn_over_Tc"].to_numpy(dtype=float)

    fig, ax_alpha = plt.subplots(figsize=(8.5, 5.2))
    ax_beta = ax_alpha.twinx()
    ax_super = ax_alpha.twinx()
    ax_super.spines["right"].set_position(("axes", 1.15))
    ax_super.spines["right"].set_visible(True)

    mask_alpha = np.isfinite(x) & np.isfinite(alpha_scaled)
    mask_beta = np.isfinite(x) & np.isfinite(beta)
    mask_super = np.isfinite(x) & np.isfinite(supercooling)

    if np.any(mask_alpha):
        ax_alpha.plot(
            x[mask_alpha],
            alpha_scaled[mask_alpha],
            lw=2.4,
            marker="o",
            ms=4.0,
            color=OBSERVABLE_COLORS["alpha"],
            label=r"$10^6\alpha$",
        )

    if np.any(mask_beta):
        ax_beta.plot(
            x[mask_beta],
            beta[mask_beta],
            lw=2.4,
            marker="s",
            ms=3.8,
            color=OBSERVABLE_COLORS["beta"],
            label=r"$\beta/H$",
        )

    if np.any(mask_super):
        ax_super.plot(
            x[mask_super],
            supercooling[mask_super],
            lw=2.4,
            marker="^",
            ms=4.0,
            color=OBSERVABLE_COLORS["supercooling"],
            label=r"$(T_c-T_n)/T_c$",
        )

    ax_alpha.set_xlabel(r"$C$")
    ax_alpha.set_ylabel(r"$10^6\alpha$", color=OBSERVABLE_COLORS["alpha"])
    ax_beta.set_ylabel(r"$\beta/H$", color=OBSERVABLE_COLORS["beta"])
    ax_super.set_ylabel(r"$(T_c-T_n)/T_c$", color=OBSERVABLE_COLORS["supercooling"])

    ax_alpha.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["alpha"])
    ax_beta.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["beta"])
    ax_super.tick_params(axis="y", labelcolor=OBSERVABLE_COLORS["supercooling"])

    ax_alpha.set_title(rf"Run 03: $\Lambda={Lambda:.0f}$ GeV, {daisy_text}")
    ax_alpha.grid(True, alpha=0.25)

    lines1, labels1 = ax_alpha.get_legend_handles_labels()
    lines2, labels2 = ax_beta.get_legend_handles_labels()
    lines3, labels3 = ax_super.get_legend_handles_labels()
    ax_alpha.legend(lines1 + lines2 + lines3, labels1 + labels2 + labels3, loc="best", fontsize=9)

    # Leave room for the third y-axis.
    fig.tight_layout(rect=(0.0, 0.0, 0.86, 1.0))

    basename = f"run03_observables_vs_C_Lambda{int(round(float(Lambda)))}_{daisy_file}"
    return list(_save_figure(fig, output_dir, basename))



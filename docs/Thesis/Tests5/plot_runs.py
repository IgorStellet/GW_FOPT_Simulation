def _safe_float(value: Any, default: float = np.nan) -> float:
    """Convert a value to float when possible."""

    if value is None:
        return default

    try:
        out = float(value)
    except (TypeError, ValueError):
        return default

    return out if np.isfinite(out) else default


def _format_float_for_label(value: float, digits: int = 4) -> str:
    """Compact float formatting for plot labels."""

    value = float(value)
    if np.isclose(value, round(value)):
        return str(int(round(value)))
    return f"{value:.{digits}g}"


def _parse_safe_float_from_path_token(token: str) -> float:
    """Parse path tokens such as '3p83' or '11p23' into floats."""

    return float(token.replace("p", ".").replace("m", "-"))


def _save_figure(fig: plt.Figure, output_dir: str | Path, basename: str) -> tuple[Path, Path]:
    """Save figure as PDF and PNG."""

    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    pdf_path = output_dir / f"{basename}.pdf"
    png_path = output_dir / f"{basename}.png"

    fig.savefig(pdf_path, bbox_inches="tight")
    fig.savefig(png_path, dpi=300, bbox_inches="tight")
    plt.close(fig)

    return pdf_path, png_path


def _read_flat_diagnostics_csv(path: str | Path) -> dict[str, Any]:
    """
    Read a thesis_diagnostics.csv file stored as quantity,value pairs.
    """

    path = Path(path)
    if not path.exists():
        return {}

    out: dict[str, Any] = {}
    with path.open("r", newline="", encoding="utf-8") as fobj:
        reader = csv.DictReader(fobj)
        for row in reader:
            key = row.get("quantity", "")
            value = row.get("value", "")
            if key:
                out[key] = value

    return out


def _first_finite_from_mapping(mapping: Mapping[str, Any], keys: Sequence[str]) -> float:
    """Return the first finite float from a list of candidate keys."""

    for key in keys:
        value = _safe_float(mapping.get(key, np.nan))
        if np.isfinite(value):
            return value
    return np.nan


def _relative_difference(new: np.ndarray, ref: np.ndarray) -> np.ndarray:
    """Compute (new-ref)/ref safely."""

    new = np.asarray(new, dtype=float)
    ref = np.asarray(ref, dtype=float)

    out = np.full_like(new, np.nan, dtype=float)
    mask = np.isfinite(new) & np.isfinite(ref) & (np.abs(ref) > 0.0)
    out[mask] = (new[mask] - ref[mask]) / ref[mask]
    return out


def _draw_detector_sensitivities(ax: plt.Axes, f_mHz: np.ndarray) -> None:
    """Draw LISA, DECIGO and BBO sensitivity curves if available."""

    if LISA_SENS is None or DECIGO_SENS is None or BBO_SENS is None:
        return

    curves = [
        ("LISA", LISA_SENS, SENSITIVITY_COLORS["LISA"]),
        ("DECIGO", DECIGO_SENS, SENSITIVITY_COLORS["DECIGO"]),
        ("BBO", BBO_SENS, SENSITIVITY_COLORS["BBO"]),
    ]

    for label, fn, color in curves:
        try:
            y = np.asarray(fn(f_mHz), dtype=float)
        except Exception as err:
            print(f"[warning] Could not evaluate {label} sensitivity: {err}")
            continue

        mask = np.isfinite(f_mHz) & np.isfinite(y) & (f_mHz > 0.0) & (y > 0.0)
        if np.any(mask):
            ax.loglog(
                f_mHz[mask],
                y[mask],
                color=color,
                lw=1.75,
                alpha=0.92,
                label=label,
            )


def _style_for_lambda(lambda_value: float) -> str:
    """Return a deterministic line style for a Lambda curve."""

    lambda_value = float(lambda_value)
    for key, style in LAMBDA_LINESTYLES.items():
        if np.isclose(lambda_value, key):
            return style
    return "-."

def _format_value_for_filename(value: float | int) -> str:
    """Format numbers safely for filenames."""

    value_float = float(value)

    if np.isclose(value_float, round(value_float)):
        text = str(int(round(value_float)))
    else:
        text = f"{value_float:.6g}"

    return text.replace("-", "m").replace(".", "p")


def _ordered_value_color_map(values: Sequence[float]) -> dict[float, str]:
    """
    Assign colors by value ordering.

    Small  -> green
    medium -> blue
    large  -> purple

    This is the same visual convention used for the three C curves in run_02.
    Here we reuse it for the three c6 curves at fixed c8.
    """

    clean_values = sorted({float(value) for value in values if np.isfinite(float(value))})

    if not clean_values:
        return {}

    if len(clean_values) == 1:
        return {clean_values[0]: C_CURVE_COLORS["medium"]}

    if len(clean_values) == 2:
        return {
            clean_values[0]: C_CURVE_COLORS["small"],
            clean_values[1]: C_CURVE_COLORS["large"],
        }

    color_map: dict[float, str] = {}

    for i, value in enumerate(clean_values):
        if i == 0:
            color_map[value] = C_CURVE_COLORS["small"]
        elif i == len(clean_values) - 1:
            color_map[value] = C_CURVE_COLORS["large"]
        else:
            color_map[value] = C_CURVE_COLORS["medium"]

    return color_map


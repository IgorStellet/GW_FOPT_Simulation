"""
plot_glauber_run_results.py
================================

Post-processing plots for the thesis Gláuber runs.

This file is meant to live in the same directory as
``thesis_eft68_gw_results_upp.py`` and the result folders

    02_Glauber_pure_benchmarks/
    03_Glauber_pure_C_scan/

It does not recompute phase transitions, bounces or spectra.  It only reads the
CSV/JSON files already generated by the production runs and builds the summary
figures used in the thesis.

Implemented plots
-----------------
Run 02:
    1. Six GW spectrum figures:
       - one figure for each Lambda and daisy choice;
       - three C curves per figure;
       - detector sensitivity curves included.

    2. Three relative-difference figures:
       - one figure for each Lambda;
       - curves show the relative daisy effect on the spectrum;
       - the code also prints and saves alpha, beta/H and supercooling changes.

Run 03:
    1. Fixed-Lambda plots of alpha(C)*1e6, beta/H(C) and (Tc-Tn)/Tc(C)
       for no-daisy.
    2. Fixed-Lambda plots of alpha(C)*1e6, beta/H(C) and (Tc-Tn)/Tc(C)
       for with-daisy.
    3. Fixed-Lambda plots of the relative daisy effect on all three
       observables.

Author workflow note
--------------------
The plotting code is intentionally independent from the simulation code: the
simulation creates the raw result folders; this file reads them and creates the
figures.  This makes the thesis post-processing reproducible and lightweight.
"""


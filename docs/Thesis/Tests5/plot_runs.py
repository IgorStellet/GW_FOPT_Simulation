@dataclass(frozen=True)
class Run02Record:
    """One run_02 point."""

    root: Path
    include_daisy: bool
    Lambda: float
    C: float
    spectrum_path: Path
    diagnostics_path: Path | None
    diagnostics: dict[str, Any]

    @property
    def daisy_label(self) -> str:
        return "with_daisy" if self.include_daisy else "no_daisy"



def _parse_lambda_C_from_folder(folder: Path) -> tuple[float, float]:
    """Parse Lambda and C from a result-folder name."""

    name = folder.name
    match = re.search(r"Glauber_C(?P<C>[0-9pm]+)_Lambda(?P<L>[0-9pm]+)", name)
    if not match:
        return np.nan, np.nan

    C = _parse_safe_float_from_path_token(match.group("C"))
    Lambda = _parse_safe_float_from_path_token(match.group("L"))
    return Lambda, C


def discover_run02_records(run02_dir: str | Path = DEFAULT_RUN02_DIR) -> list[Run02Record]:
    """
    Find all run_02 folders that contain a GW spectrum table.
    """

    run02_dir = Path(run02_dir)
    records: list[Run02Record] = []

    for daisy_folder, include_daisy in [("no_daisy", False), ("with_daisy", True)]:
        parent = run02_dir / daisy_folder
        if not parent.exists():
            print(f"[warning] Missing run_02 folder: {parent}")
            continue

        for folder in sorted(parent.glob("results_*")):
            if not folder.is_dir():
                continue

            spectrum_path = folder / "G0_gw_spectrum.csv"
            if not spectrum_path.exists():
                print(f"[run_02] Skipping {folder.name}: missing G0_gw_spectrum.csv")
                continue

            diagnostics_path = folder / "thesis_diagnostics.csv"
            diagnostics = _read_flat_diagnostics_csv(diagnostics_path)

            Lambda = _first_finite_from_mapping(
                diagnostics,
                ["model_parameters.Lambda_glauber", "gw_thermodynamics.Lambda_glauber"],
            )
            C = _first_finite_from_mapping(
                diagnostics,
                ["model_parameters.C_glauber", "gw_thermodynamics.C_glauber"],
            )

            if not np.isfinite(Lambda) or not np.isfinite(C):
                Lambda_folder, C_folder = _parse_lambda_C_from_folder(folder)
                Lambda = Lambda if np.isfinite(Lambda) else Lambda_folder
                C = C if np.isfinite(C) else C_folder

            if not np.isfinite(Lambda) or not np.isfinite(C):
                print(f"[run_02] Skipping {folder.name}: could not infer Lambda/C")
                continue

            records.append(
                Run02Record(
                    root=folder,
                    include_daisy=include_daisy,
                    Lambda=float(Lambda),
                    C=float(C),
                    spectrum_path=spectrum_path,
                    diagnostics_path=diagnostics_path if diagnostics_path.exists() else None,
                    diagnostics=diagnostics,
                )
            )

    # If the same point was run more than once, the automatic folder name can
    # change because the field cutoff appears in the path.  For plotting we keep
    # only the most recent folder for each (daisy, Lambda, C) point.  This makes
    # the post-processing robust under repeated runs.
    latest: dict[tuple[bool, float, float], Run02Record] = {}
    for record in records:
        key = (
            bool(record.include_daisy),
            round(float(record.Lambda), 8),
            round(float(record.C), 8),
        )
        previous = latest.get(key)
        if previous is None or record.root.stat().st_mtime >= previous.root.stat().st_mtime:
            latest[key] = record

    return sorted(
        latest.values(),
        key=lambda item: (item.include_daisy, item.Lambda, item.C),
    )


def _C_color_map_for_records(records: Sequence[Run02Record]) -> dict[float, str]:
    """
    Assign colors by C ordering: small green, medium blue, large purple.
    """

    Cs = sorted({float(record.C) for record in records})
    if not Cs:
        return {}

    if len(Cs) == 1:
        return {Cs[0]: C_CURVE_COLORS["medium"]}

    if len(Cs) == 2:
        return {Cs[0]: C_CURVE_COLORS["small"], Cs[1]: C_CURVE_COLORS["large"]}

    # The thesis benchmark uses three C values.  If more exist, the extremes
    # keep the requested colors and the interior points use blue.
    color_map: dict[float, str] = {}
    for i, C in enumerate(Cs):
        if i == 0:
            color_map[C] = C_CURVE_COLORS["small"]
        elif i == len(Cs) - 1:
            color_map[C] = C_CURVE_COLORS["large"]
        else:
            color_map[C] = C_CURVE_COLORS["medium"]
    return color_map


def _load_spectrum(path: str | Path, spectrum_column: str = "omega_total_max_turb") -> tuple[np.ndarray, np.ndarray, pd.DataFrame]:
    """Load frequency and selected GW spectrum column."""

    df = pd.read_csv(path)

    if "f_mHz" not in df.columns:
        raise ValueError(f"{path} does not contain an f_mHz column.")

    if spectrum_column not in df.columns:
        fallback = "omega_total" if "omega_total" in df.columns else None
        if fallback is None:
            raise ValueError(
                f"{path} does not contain {spectrum_column!r} or 'omega_total'."
            )
        spectrum_column = fallback

    f = np.asarray(df["f_mHz"], dtype=float)
    y = np.asarray(df[spectrum_column], dtype=float)
    return f, y, df


def _load_spectrum_epsilon_band(path: str | Path) -> tuple[np.ndarray, np.ndarray, np.ndarray, pd.DataFrame]:
    """
    Load the two GW spectra used to represent the turbulence uncertainty band.

    Solid line: epsilon_turb = 0, read from ``omega_total_no_turb``.
    Dashed line: epsilon_turb = 1, read from ``omega_total_max_turb``.

    Older result folders may only contain ``omega_total``.  In that case the
    same curve is used for both entries, so the plotting code remains backward
    compatible.
    """

    df = pd.read_csv(path)

    if "f_mHz" not in df.columns:
        raise ValueError(f"{path} does not contain an f_mHz column.")

    f = np.asarray(df["f_mHz"], dtype=float)

    if "omega_total_no_turb" in df.columns:
        omega_eps0 = np.asarray(df["omega_total_no_turb"], dtype=float)
    elif "omega_total" in df.columns:
        omega_eps0 = np.asarray(df["omega_total"], dtype=float)
    else:
        raise ValueError(f"{path} does not contain omega_total_no_turb or omega_total.")

    if "omega_total_max_turb" in df.columns:
        omega_eps1 = np.asarray(df["omega_total_max_turb"], dtype=float)
    elif "omega_total" in df.columns:
        omega_eps1 = np.asarray(df["omega_total"], dtype=float)
    else:
        omega_eps1 = omega_eps0.copy()

    return f, omega_eps0, omega_eps1, df



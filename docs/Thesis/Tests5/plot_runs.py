def plot_run02_fixed_lambda_spectra(
    *,
    run02_dir: str | Path = DEFAULT_RUN02_DIR,
    spectrum_column: str = "omega_total_max_turb",
    y_limits: tuple[float, float] = (1e-21, 1e-9),
    x_limits: tuple[float, float] = (1e-3, 1e5),
) -> list[Path]:
    """
    Build six fixed-Lambda spectrum plots for run_02.

    For each Lambda and daisy choice, the three C curves are shown together.
    Each C is represented by the same visual convention used in the thesis GW
    plots:

    - solid curve: epsilon_turb = 0;
    - dashed curve: epsilon_turb = 1;
    - filled band: 0 <= epsilon_turb <= 1.

    The ``spectrum_column`` argument is kept for backward compatibility with
    earlier calls, but the preferred run_02 visualization uses the full
    epsilon_turb band whenever the required columns are available.
    """

    _ = spectrum_column  # kept only for backward-compatible function calls.

    records = discover_run02_records(run02_dir)
    output_dir = Path(run02_dir)
    saved: list[Path] = []

    for include_daisy in [False, True]:
        subset_daisy = [record for record in records if record.include_daisy == include_daisy]
        daisy_name = "with daisy" if include_daisy else "no daisy"
        daisy_file = "with_daisy" if include_daisy else "no_daisy"

        for Lambda in sorted({record.Lambda for record in subset_daisy}):
            subset = [record for record in subset_daisy if np.isclose(record.Lambda, Lambda)]
            if not subset:
                continue

            color_map = _C_color_map_for_records(subset)

            fig, ax = plt.subplots(figsize=(8.2, 5.2))

            f_ref: np.ndarray | None = None
            band_label_used = False

            for record in sorted(subset, key=lambda item: item.C):
                f, omega_eps0, omega_eps1, _df = _load_spectrum_epsilon_band(record.spectrum_path)
                if f_ref is None:
                    f_ref = f

                color = color_map.get(record.C, C_CURVE_COLORS["medium"])

                mask0 = np.isfinite(f) & np.isfinite(omega_eps0) & (f > 0.0) & (omega_eps0 > 0.0)
                mask1 = np.isfinite(f) & np.isfinite(omega_eps1) & (f > 0.0) & (omega_eps1 > 0.0)
                mask_band = mask0 & mask1

                if not np.any(mask0) and not np.any(mask1):
                    print(f"[run_02] No positive spectrum for {record.root}")
                    continue

                if np.any(mask_band):
                    y_lo = np.minimum(omega_eps0[mask_band], omega_eps1[mask_band])
                    y_hi = np.maximum(omega_eps0[mask_band], omega_eps1[mask_band])
                    ax.fill_between(
                        f[mask_band],
                        y_lo,
                        y_hi,
                        color=color,
                        alpha=0.15,
                        linewidth=0.0,
                        label=(r"$0\leq\epsilon_{\rm turb}\leq1$" if not band_label_used else "_nolegend_"),
                    )
                    band_label_used = True

                if np.any(mask0):
                    ax.loglog(
                        f[mask0],
                        omega_eps0[mask0],
                        lw=2.45,
                        ls="-",
                        color=color,
                        label=rf"$C={record.C:.4g}$",
                    )

                if np.any(mask1):
                    ax.loglog(
                        f[mask1],
                        omega_eps1[mask1],
                        lw=2.15,
                        ls="--",
                        color=color,
                        label="_nolegend_",
                    )

            if f_ref is not None:
                _draw_detector_sensitivities(ax, f_ref)

            style_handles = [
                Line2D([0], [0], color="black", lw=2.2, ls="-", label=r"$\epsilon_{\rm turb}=0$"),
                Line2D([0], [0], color="black", lw=2.0, ls="--", label=r"$\epsilon_{\rm turb}=1$"),
            ]
            handles, labels = ax.get_legend_handles_labels()
            handles = handles + style_handles
            labels = labels + [handle.get_label() for handle in style_handles]

            ax.set_xlim(*x_limits)
            ax.set_ylim(*y_limits)
            ax.set_xlabel(r"$f$ [mHz]")
            ax.set_ylabel(r"$h^2\Omega_{\rm GW}(f)$")
            ax.set_title(rf"Run 02: $\Lambda={Lambda:.0f}$ GeV, {daisy_name}")
            ax.grid(True, which="both", alpha=0.25)
            ax.legend(handles, labels, loc="best", fontsize=8)
            fig.tight_layout()

            basename = f"run02_GW_spectra_Lambda{int(round(Lambda))}_{daisy_file}"
            pdf_path, png_path = _save_figure(fig, output_dir, basename)
            saved.extend([pdf_path, png_path])

            print(f"[run_02] Saved {pdf_path}")

    return saved


def _run02_record_lookup(records: Sequence[Run02Record]) -> dict[tuple[bool, float, float], Run02Record]:
    """Create a lookup keyed by (include_daisy, Lambda, rounded C)."""

    lookup: dict[tuple[bool, float, float], Run02Record] = {}
    for record in records:
        key = (record.include_daisy, round(record.Lambda, 8), round(record.C, 8))
        lookup[key] = record
    return lookup


def _run02_matched_pairs(records: Sequence[Run02Record]) -> list[tuple[Run02Record, Run02Record]]:
    """
    Pair no-daisy and with-daisy run_02 records at the same Lambda and C.
    """

    lookup = _run02_record_lookup(records)
    pairs: list[tuple[Run02Record, Run02Record]] = []

    no_daisy_keys = [key for key in lookup if key[0] is False]
    for _, Lambda_key, C_key in sorted(no_daisy_keys, key=lambda item: (item[1], item[2])):
        no_key = (False, Lambda_key, C_key)
        yes_key = (True, Lambda_key, C_key)
        if yes_key in lookup:
            pairs.append((lookup[no_key], lookup[yes_key]))
        else:
            print(f"[run_02] No with-daisy match for Lambda={Lambda_key}, C={C_key}")

    return pairs


def _diagnostic_observables(record: Run02Record) -> dict[str, float]:
    """Extract alpha, beta/H and supercooling from one run_02 record."""

    diag = record.diagnostics
    Tc = _first_finite_from_mapping(diag, ["key_temperatures.Tc", "transition_observables.Tc"])
    Tn = _first_finite_from_mapping(diag, ["key_temperatures.Tn", "transition_observables.Tn"])

    supercooling = _first_finite_from_mapping(
        diag,
        [
            "key_temperatures.Tc_minus_Tn_over_Tc",
            "transition_observables.supercooling_fraction",
            "transition_observables.Tc_minus_Tn_over_Tc",
        ],
    )
    if not np.isfinite(supercooling) and np.isfinite(Tc) and np.isfinite(Tn) and Tc > 0.0:
        supercooling = (Tc - Tn) / Tc

    return {
        "alpha": _first_finite_from_mapping(
            diag,
            ["gw_scales.gw_alpha", "gw_thermodynamics.alpha", "gw_thermodynamics.T_alpha"],
        ),
        "beta_over_H": _first_finite_from_mapping(
            diag,
            ["gw_scales.gw_beta_over_H", "gw_thermodynamics.beta_over_H"],
        ),
        "supercooling": supercooling,
    }


def plot_run02_daisy_relative_difference(
    *,
    run02_dir: str | Path = DEFAULT_RUN02_DIR,
    spectrum_column: str = "omega_total_max_turb",
    x_limits: tuple[float, float] = (1e-3, 1e5),
    y_limits: tuple[float, float] | None = None,
) -> tuple[list[Path], Path]:
    """
    Plot the relative daisy effect on the GW spectrum for run_02.

    The relative difference is

        (Omega_with_daisy - Omega_no_daisy) / Omega_no_daisy.

    The function also prints and saves the relative differences of alpha,
    beta/H and (Tc-Tn)/Tc.
    """

    records = discover_run02_records(run02_dir)
    pairs = _run02_matched_pairs(records)
    output_dir = Path(run02_dir)
    saved: list[Path] = []
    diagnostic_rows: list[dict[str, Any]] = []

    for Lambda in sorted({pair[0].Lambda for pair in pairs}):
        subset = [pair for pair in pairs if np.isclose(pair[0].Lambda, Lambda)]
        if not subset:
            continue

        color_map = _C_color_map_for_records([pair[0] for pair in subset])

        fig, ax = plt.subplots(figsize=(8.2, 4.8))

        for no_record, yes_record in sorted(subset, key=lambda pair: pair[0].C):
            f_no, y_no, _ = _load_spectrum(no_record.spectrum_path, spectrum_column=spectrum_column)
            f_yes, y_yes, _ = _load_spectrum(yes_record.spectrum_path, spectrum_column=spectrum_column)

            if f_no.shape != f_yes.shape or not np.allclose(f_no, f_yes, rtol=1e-8, atol=0.0):
                y_yes = np.interp(f_no, f_yes, y_yes, left=np.nan, right=np.nan)

            rel = _relative_difference(y_yes, y_no)
            mask = np.isfinite(f_no) & np.isfinite(rel) & (f_no > 0.0)
            if not np.any(mask):
                continue

            ax.semilogx(
                f_no[mask],
                rel[mask],
                lw=2.35,
                color=color_map.get(no_record.C, C_CURVE_COLORS["medium"]),
                label=rf"$C={no_record.C:.4g}$",
            )

            obs_no = _diagnostic_observables(no_record)
            obs_yes = _diagnostic_observables(yes_record)

            row: dict[str, Any] = {
                "Lambda_GeV": no_record.Lambda,
                "C": no_record.C,
                "spectrum_relative_min": float(np.nanmin(rel[mask])) if np.any(mask) else np.nan,
                "spectrum_relative_max": float(np.nanmax(rel[mask])) if np.any(mask) else np.nan,
            }

            for key in ["alpha", "beta_over_H", "supercooling"]:
                no_val = obs_no[key]
                yes_val = obs_yes[key]
                row[f"{key}_no_daisy"] = no_val
                row[f"{key}_with_daisy"] = yes_val
                row[f"{key}_relative_difference"] = (
                    (yes_val - no_val) / no_val
                    if np.isfinite(no_val) and np.isfinite(yes_val) and no_val != 0.0
                    else np.nan
                )

            diagnostic_rows.append(row)

        ax.axhline(0.0, color="black", lw=1.0, ls="--", alpha=0.7)
        ax.set_xlim(*x_limits)
        if y_limits is not None:
            ax.set_ylim(*y_limits)
        ax.set_xlabel(r"$f$ [mHz]")
        ax.set_ylabel(r"$(\Omega_{\rm daisy}-\Omega_{0})/\Omega_{0}$")
        ax.set_title(rf"Run 02: relative daisy effect, $\Lambda={Lambda:.0f}$ GeV")
        ax.grid(True, which="both", alpha=0.25)
        ax.legend(loc="best", fontsize=9)
        fig.tight_layout()

        basename = f"run02_relative_daisy_spectrum_Lambda{int(round(Lambda))}"
        pdf_path, png_path = _save_figure(fig, output_dir, basename)
        saved.extend([pdf_path, png_path])
        print(f"[run_02] Saved {pdf_path}")

    diagnostics_path = output_dir / "run02_daisy_relative_diagnostics.csv"
    if diagnostic_rows:
        df_diag = pd.DataFrame(diagnostic_rows).sort_values(["Lambda_GeV", "C"])
        df_diag.to_csv(diagnostics_path, index=False)

        print("\n" + "=" * 76)
        print("Run 02 – relative daisy diagnostics")
        print("=" * 76)
        for _, row in df_diag.iterrows():
            print(
                f"Lambda={row['Lambda_GeV']:.0f} GeV, C={row['C']:.5g} | "
                f"dalpha/alpha={row['alpha_relative_difference']:.6g}, "
                f"dbeta/beta={row['beta_over_H_relative_difference']:.6g}, "
                f"dsc/sc={row['supercooling_relative_difference']:.6g}"
            )
        print(f"Saved: {diagnostics_path}")
        print("=" * 76)
    else:
        print("[run_02] No diagnostic pairs found for daisy comparison.")

    return saved, diagnostics_path



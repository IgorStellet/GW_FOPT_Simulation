def gather_thesis_diagnostics(summary: dict[str, Any], *, output_dir: str | Path, basename: str = "thesis_diagnostics", label: str = "thesis_run") -> dict[str, Any]:
    diagnostics: dict[str, Any] = {
        "label": label,
        "model_parameters": dict(summary.get("model_parameters", {}) or {}),
        "scan_parameters": dict(summary.get("scan_parameters", {}) or {}),
        "key_temperatures": dict(summary.get("key_temperatures", {}) or {}),
        "gw_scales": dict(summary.get("gw_scales", {}) or {}),
    }
    if "gw" in summary:
        diagnostics["gw_peaks"] = dict(summary["gw"].get("peaks", {}) or {})
        diagnostics["gw_run_parameters"] = dict(summary["gw"].get("parameters", {}) or {})
    if "bounce" in summary:
        b = summary["bounce"]
        diagnostics["S3"] = float(b.get("S3", np.nan))
        diagnostics["S3_over_T"] = float(b.get("S3_over_T", np.nan))
        try:
            diagnostics["bounce_diagnostics"] = gather_diagnostics(b["instanton"], b["profile"], label=label, transition_summary=summary)
        except Exception as err:
            diagnostics["bounce_diagnostics_error"] = repr(err)
    try:
        save_diagnostics_summary(diagnostics, output_dir, basename=basename, fmt="json")
    except Exception:
        save_json(diagnostics, Path(output_dir) / f"{basename}.json")
    flat = {}
    def flatten(d: Mapping[str, Any], prefix: str = "") -> None:
        for k, v in d.items():
            key = f"{prefix}.{k}" if prefix else str(k)
            if isinstance(v, Mapping):
                flatten(v, key)
            else:
                flat[key] = v
    flatten(diagnostics)
    with open(Path(output_dir) / f"{basename}.csv", "w", newline="", encoding="utf-8") as fobj:
        writer = csv.writer(fobj)
        writer.writerow(["quantity", "value"])
        for k, v in flat.items():
            writer.writerow([k, v])
    summary["diagnostics"] = diagnostics
    return diagnostics



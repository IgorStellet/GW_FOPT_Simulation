def _format_value_for_path(value: float | int) -> str:
    value_float = float(value)
    if np.isclose(value_float, round(value_float)):
        text = str(int(round(value_float)))
    else:
        text = f"{value_float:.6g}"
    return text.replace("-", "m").replace(".", "p")


def build_results_directory_name(
    params: EFT68Parameters,
    *,
    run_tag: str | None = None,
) -> str:
    """Build a clear, automatic output directory name."""

    if params.use_glauber_measure:
        glauber = (
            f"Glauber_C{_format_value_for_path(params.C_glauber)}_"
            f"Lambda{_format_value_for_path(params.Lambda_glauber)}"
        )
    else:
        glauber = "Glauber_off"

    name = (
        "results_eft68_"
        f"c6f2{_format_value_for_path(params.c6_over_f2_TeV2)}_"
        f"c8f4{_format_value_for_path(params.c8_over_f4_TeV4)}_"
        f"f{_format_value_for_path(params.f_GeV)}_"
        f"mh{_format_value_for_path(params.m_h)}_"
        f"{glauber}"
    )
    if run_tag is not None and str(run_tag).strip():
        name += "_" + str(run_tag).strip().replace(" ", "_")
    return name


def make_results_directory(
    params: EFT68Parameters,
    *,
    run_tag: str | None = None,
    base_dir: str | Path = ".",
) -> Path:
    output_dir = Path(base_dir) / build_results_directory_name(params, run_tag=run_tag)
    output_dir.mkdir(parents=True, exist_ok=True)
    return output_dir


def save_figure(
    fig: plt.Figure,
    output_dir: str | Path,
    filename: str,
    *,
    close: bool = True,
    save_png: bool = True,
) -> Path:
    """Save a Matplotlib figure as PDF, optionally also as PNG."""

    output_dir = Path(output_dir)
    pdf_path = output_dir / f"{filename}.pdf"
    fig.savefig(pdf_path, bbox_inches="tight")
    if save_png:
        fig.savefig(output_dir / f"{filename}.png", dpi=300, bbox_inches="tight")
    if close:
        plt.close(fig)
    return pdf_path


def save_json(data: Mapping[str, Any], path: str | Path) -> Path:
    path = Path(path)

    def convert(obj: Any) -> Any:
        if isinstance(obj, np.generic):
            return obj.item()
        if isinstance(obj, np.ndarray):
            return obj.tolist()
        if isinstance(obj, Path):
            return str(obj)
        if isinstance(obj, Mapping):
            return {str(k): convert(v) for k, v in obj.items()}
        if isinstance(obj, (list, tuple)):
            return [convert(x) for x in obj]
        return obj

    with open(path, "w", encoding="utf-8") as fobj:
        json.dump(convert(dict(data)), fobj, indent=2)
    return path



def finite_minimum(phi: np.ndarray, y: np.ndarray) -> tuple[float, float] | None:
    finite = np.isfinite(y)
    if not np.any(finite):
        return None
    idx = np.flatnonzero(finite)[np.argmin(y[finite])]
    return float(phi[idx]), float(y[idx])


def check_zero_temperature_physicality(
    params: EFT68Parameters,
    *,
    n_grid: int = 3000,
    include_zeroT_loops: bool = False,
    include_glauber: bool | None = None,
) -> dict[str, Any]:
    """Check the T=0 EFT conditions inside 0 <= phi <= f."""
    phi = np.linspace(0.0, params.f_GeV, int(n_grid))
    V_tree = eft68_tree_potential(phi, params)
    V_eff = deltaV(
        phi,
        0.0,
        params,
        include_zeroT_loops=include_zeroT_loops,
        include_thermal=False,
        include_daisy=False,
        include_glauber=include_glauber,
    )
    Vv_tree = float(eft68_tree_potential(params.v, params))
    mask = phi > params.v
    min_after_v = float(np.nanmin(V_tree[mask])) if np.any(mask) else np.nan
    min_eff = finite_minimum(phi, V_eff)
    # Numerical derivatives of the full chosen zero-T potential at v.
    h = 1.0e-3 * params.v
    f0 = float(V_effective_potential(params.v, 0.0, params, include_zeroT_loops=include_zeroT_loops, include_thermal=False, include_daisy=False, include_glauber=include_glauber))
    fp = float(V_effective_potential(params.v + h, 0.0, params, include_zeroT_loops=include_zeroT_loops, include_thermal=False, include_daisy=False, include_glauber=include_glauber))
    fm = float(V_effective_potential(params.v - h, 0.0, params, include_zeroT_loops=include_zeroT_loops, include_thermal=False, include_daisy=False, include_glauber=include_glauber))
    dV = (fp - fm) / (2.0 * h)
    d2V = (fp - 2.0 * f0 + fm) / h**2
    return {
        "lambda_tree": params.lambda_tree,
        "mu2_tree": params.mu2_tree,
        "a0_mean_field": params.a0_mean_field,
        "combo_TeV2": params.combo_TeV2,
        "upper_c6_over_f2_TeV2_from_Eq_2_14": params.physicality_upper_c6_TeV2,
        "passes_Eq_2_14": params.c6_over_f2_TeV2 < params.physicality_upper_c6_TeV2,
        "passes_EFT_tree_condition_Vv_lower_than_v_to_f": bool(Vv_tree < min_after_v),
        "min_after_v_tree": min_after_v,
        "V_tree_v": Vv_tree,
        "sampled_min_phi_full_zeroT": None if min_eff is None else min_eff[0],
        "sampled_min_deltaV_full_zeroT": None if min_eff is None else min_eff[1],
        "dV_at_v_numeric": dV,
        "d2V_at_v_numeric": d2V,
        "mh2_target": params.m_h**2,
    }


def find_extrema_1d(
    phi: np.ndarray,
    V: np.ndarray,
) -> list[dict[str, float | str]]:
    """Fast grid-based extrema finder used for diagnostics and mean-field scans."""
    phi = np.asarray(phi, dtype=float)
    V = np.asarray(V, dtype=float)
    finite = np.isfinite(V)
    if np.count_nonzero(finite) < 5:
        return []
    dV = np.gradient(V, phi, edge_order=2)
    roots: list[float] = []
    for i in range(phi.size - 1):
        if not (np.isfinite(dV[i]) and np.isfinite(dV[i + 1])):
            continue
        if dV[i] == 0.0:
            root = phi[i]
        elif dV[i] * dV[i + 1] < 0.0:
            root = phi[i] - dV[i] * (phi[i + 1] - phi[i]) / (dV[i + 1] - dV[i])
        else:
            continue
        if phi[0] < root < phi[-1] and all(abs(root - r) > 2.0 * (phi[1] - phi[0]) for r in roots):
            roots.append(float(root))
    records: list[dict[str, float | str]] = []
    d2V_grid = np.gradient(dV, phi, edge_order=2)
    if V[1] > V[0]:
        records.append({"phi": 0.0, "V": float(V[0]), "kind": "boundary_minimum"})
    for root in roots:
        Vroot = float(np.interp(root, phi, V))
        curv = float(np.interp(root, phi, d2V_grid))
        kind = "minimum" if curv > 0.0 else "maximum" if curv < 0.0 else "flat"
        records.append({"phi": root, "V": Vroot, "curvature": curv, "kind": kind})
    if records:
        mins = [r for r in records if str(r["kind"]).endswith("minimum")]
        if mins:
            best = min(mins, key=lambda r: float(r["V"]))
            best["role"] = "global_minimum"
    return records


def mean_field_vc_tc(params: EFT68Parameters) -> dict[str, float | None]:
    """
    Mean-field critical temperature and v_c/T_c.

    This uses the analytic v_c condition, but computes T_c from the
    degeneracy equation directly. It avoids the unstable complex branch in
    the previous implementation.
    """

    k6 = params.k6_GeV2
    k8 = params.k8_GeV4
    lam = params.lambda_tree
    a0 = params.a0_mean_field
    mu2 = params.mu2_tree

    if a0 <= 0.0:
        return {"v_c": None, "T_c": None, "v_c_over_T_c": None}

    candidates: list[tuple[float, float]] = []

    if abs(k8) > 0.0:
        disc = k6**2 / (9.0 * k8**2) - lam / (3.0 * k8)

        if disc >= 0.0:
            sqrt_disc = np.sqrt(disc)

            for sign in (+1.0, -1.0):
                vc2 = -2.0 * k6 / (3.0 * k8) + 2.0 * sign * sqrt_disc

                if vc2 <= 0.0:
                    continue

                Tc2 = (
                    mu2
                    - 0.5 * lam * vc2
                    - 0.25 * k6 * vc2**2
                    - 0.125 * k8 * vc2**3
                ) / a0

                if Tc2 > 0.0:
                    candidates.append((float(np.sqrt(vc2)), float(np.sqrt(Tc2))))

    else:
        if k6 > 0.0 and lam < 0.0:
            vc2 = -lam / k6

            Tc2 = (
                mu2
                - 0.5 * lam * vc2
                - 0.25 * k6 * vc2**2
            ) / a0

            if vc2 > 0.0 and Tc2 > 0.0:
                candidates.append((float(np.sqrt(vc2)), float(np.sqrt(Tc2))))

    if not candidates:
        return {"v_c": None, "T_c": None, "v_c_over_T_c": None}

    valid = [(vc, Tc) for vc, Tc in candidates if vc <= params.f_GeV]
    vc, Tc = valid[0] if valid else candidates[0]

    return {
        "v_c": vc,
        "T_c": Tc,
        "v_c_over_T_c": vc / Tc if Tc > 0.0 else None,
    }



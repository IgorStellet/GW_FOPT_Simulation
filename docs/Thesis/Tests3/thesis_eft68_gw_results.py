def _finite_positive_peak(f: np.ndarray, y: np.ndarray) -> tuple[float, float]:
    mask = np.isfinite(f) & np.isfinite(y) & (y > 0.0)
    if not np.any(mask):
        return np.nan, np.nan
    ff = f[mask]
    yy = y[mask]
    idx = np.argmax(yy)
    return float(ff[idx]), float(yy[idx])


def _existing_S3_at_Tn(summary: Mapping[str, Any], Tn: float) -> float:
    b = summary.get("bounce", None)
    if isinstance(b, Mapping) and _as_finite_float_or_none(b.get("S3")) is not None:
        return float(b["S3"])
    key_T = summary.get("key_temperatures", {}) or {}
    if _as_finite_float_or_none(key_T.get("S_at_Tn")) is not None:
        return float(key_T["S_at_Tn"])
    if _as_finite_float_or_none(key_T.get("S_over_Tn")) is not None:
        return float(key_T["S_over_Tn"]) * Tn
    return np.nan


def compute_gw_spectrum_summary(
    summary: dict[str, Any],
    *,
    f_min_mHz: float = 1e-3,
    f_max_mHz: float = 1e5,
    n_freq: int = 2000,
    g_star: float = 106.75,
    v_w: float = 0.95,
    dT_fraction: float = 1e-3,
    include_sw: bool = True,
    include_turb: bool = True,
    include_coll: bool = True,
    epsilon_turb: float | None = 1.0,
    kappa_coll: float | None = None,
    beta_over_H_override: float | None = None,
) -> dict[str, Any]:
    if summary.get("main_transition", None) is None:
        raise RuntimeError("No main transition found.")
    gw_calc = _build_gw_calculator_from_summary(summary)
    if gw_calc is None:
        raise RuntimeError("Could not build GW calculator.")
    T_star = _as_finite_float_or_none((summary.get("key_temperatures", {}) or {}).get("Tn"))
    if T_star is None:
        raise RuntimeError("Missing Tn for GW computation.")
    S3_Tn = _existing_S3_at_Tn(summary, T_star)
    if np.isfinite(S3_Tn):
        try:
            gw_calc._S3_cache[float(T_star)] = float(S3_Tn)
        except Exception:
            pass
    alpha = float(gw_calc.alpha(T_star, g_star))
    dT = max(1.0e-4, dT_fraction * max(T_star, 1.0))
    beta_over_H = float(beta_over_H_override) if beta_over_H_override is not None else float(gw_calc.beta_over_H(T_star, dT))
    f_mHz = np.logspace(np.log10(f_min_mHz), np.log10(f_max_mHz), int(n_freq))
    spectra = gw_calc.omega_total_h2(f_mHz, alpha=alpha, beta_over_H=beta_over_H, T_star=T_star, g_star=g_star, v_w=v_w, include_sw=include_sw, include_turb=include_turb, include_coll=include_coll, epsilon_turb=epsilon_turb, kappa_coll=kappa_coll)
    spectra_no_turb = gw_calc.omega_total_h2(f_mHz, alpha=alpha, beta_over_H=beta_over_H, T_star=T_star, g_star=g_star, v_w=v_w, include_sw=include_sw, include_turb=include_turb, include_coll=include_coll, epsilon_turb=0.0, kappa_coll=kappa_coll)
    spectra_max_turb = gw_calc.omega_total_h2(f_mHz, alpha=alpha, beta_over_H=beta_over_H, T_star=T_star, g_star=g_star, v_w=v_w, include_sw=include_sw, include_turb=include_turb, include_coll=include_coll, epsilon_turb=1.0, kappa_coll=kappa_coll)
    peaks = {}
    for key in ("sw", "turb", "coll", "total"):
        fp, op = _finite_positive_peak(f_mHz, spectra[key])
        peaks[key] = {"f_peak_mHz": fp, "f_peak_Hz": 1e-3 * fp if np.isfinite(fp) else np.nan, "omega_peak": op}
    summary["gw_scales"] = {"gw_T_star_GeV": T_star, "gw_alpha": alpha, "gw_beta_over_H": beta_over_H, "gw_v_w": v_w, "gw_g_star": g_star}
    summary["gw"] = {"f_mHz": f_mHz, "f_Hz": 1e-3 * f_mHz, "spectra": spectra, "spectra_no_turb": spectra_no_turb, "spectra_max_turb": spectra_max_turb, "peaks": peaks, "parameters": {"g_star": g_star, "v_w": v_w, "epsilon_turb": epsilon_turb, "frequency_unit": "mHz"}}
    print("\nGW summary: alpha =", alpha, " beta/H =", beta_over_H, " peak total =", peaks["total"]["omega_peak"])
    return summary["gw"]



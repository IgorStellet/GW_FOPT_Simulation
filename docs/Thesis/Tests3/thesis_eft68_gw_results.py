"""
thesis_eft68_gw_results.py
==========================

Objective
---------
Build thesis-ready numerical results for electroweak first-order phase
transitions generated by higher-dimensional scalar-sector operators,
following the dimension-six/dimension-eight EFT setup of Chala, Krause and
Nardini (arXiv:1802.02168):

    L = L_SM + (c6/f^2)(H†H)^3 + (c8/f^4)(H†H)^4.

The code is organized as a guided workflow:

A. Zero-temperature EFT potential and article benchmarks.
B. EFT physicality/validity checks below the cutoff f.
C. Finite-temperature potential shapes.
D. Mean-field scan and fast transition pre-diagnosis.
E. CosmoTransitions phase tracing, Tc, Tn and thermal plots.
F. Bounce solution at Tn and bounce diagnostics.
G. Gravitational-wave spectra and run diagnostics.

The Gláuber functional-measure term is kept as an optional add-on, but the
default workflow first reproduces the pure dimension-six/eight EFT.  When the
Gláuber term is enabled, its counterterm form is the same one used in the
functional-measure paper, so its first and second derivatives vanish at the
input electroweak vacuum v.  The code still performs explicit T=0 checks.
"""


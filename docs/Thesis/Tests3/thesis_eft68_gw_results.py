def example_E4_free_energy_difference(summary: dict[str, Any], *, output_dir: str | Path, show: bool = False) -> Path | None:
    try:
        _, false_phase, true_phase, *_rest, key_T = _main_transition_objects(summary)
    except RuntimeError as err:
        print(f"[E4] {err}; skipping.")
        return None
    V_XT = summary["V_XT"]
    Tmin = max(float(np.min(false_phase.T)), float(np.min(true_phase.T)))
    Tmax = min(float(np.max(false_phase.T)), float(np.max(true_phase.T)))
    if Tmax <= Tmin:
        return None
    T_vals = np.linspace(Tmin, Tmax, 350)
    dV = []
    for T in T_vals:
        xf = np.asarray(false_phase.valAt(float(T)), dtype=float)
        xt = np.asarray(true_phase.valAt(float(T)), dtype=float)

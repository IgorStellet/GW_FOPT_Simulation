def thesis_nucleation_criterion(S: float, T: float) -> float:
    return S / (T + 1.0e-100) - 140.0


def V_effective_potential_XT(
    X: ArrayLike | float,
    T: ArrayLike | float,
    params: EFT68Parameters,
    **kwargs: Any,
) -> np.ndarray:
    X_arr = np.asarray(X, dtype=float)
    if X_arr.ndim == 0:
        phi = X_arr
    elif X_arr.ndim == 1:
        phi = X_arr[0] if X_arr.shape[0] == 1 else X_arr
    else:
        phi = X_arr[..., 0]
    return np.asarray(V_effective_potential(phi, float(np.asarray(T)), params, **kwargs), dtype=float)


def build_finite_temperature_problem(
    params: EFT68Parameters,
    *,
    x_eps: float = 1e-3,
    T_eps: float = 1e-2,
    deriv_order: int = 4,
    include_zeroT_loops: bool = True,
    include_scalar_loops: bool = True,
    renormalize_zeroT_loops: bool = True,
    include_thermal: bool = True,
    include_scalar_thermal: bool = True,
    include_daisy: bool = False,
    thermal_approx: str = "spline",
) -> dict[str, Any]:
    def Vtot_XT(X: ArrayLike | float, T: ArrayLike | float) -> np.ndarray:
        return V_effective_potential_XT(
            X,
            T,
            params,
            include_zeroT_loops=include_zeroT_loops,
            include_scalar_loops=include_scalar_loops,
            renormalize_zeroT_loops=renormalize_zeroT_loops,
            include_thermal=include_thermal,
            include_scalar_thermal=include_scalar_thermal,
            include_daisy=include_daisy,
            thermal_approx=thermal_approx,
        )

    derivs = build_finite_T_derivatives(
        Vtot=Vtot_XT,
        Ndim=1,
        x_eps=x_eps,
        T_eps=T_eps,
        deriv_order=deriv_order,
        X_ref=np.array([0.0]),
    )
    return {
        "V_raw_XT": Vtot_XT,
        "V_XT": derivs.V,
        "dVdphi_XT": derivs.gradV,
        "hessV_XT": derivs.hessV,
        "dVdT_XT": derivs.dV_dT,
        "dgradT_XT": derivs.dgradV_dT,
    }


def _as_finite_float_or_none(value: Any) -> float | None:
    if value is None:
        return None
    try:
        out = float(value)
    except (TypeError, ValueError):
        return None
    return out if np.isfinite(out) else None


def _extract_critical_temperature(summary: Mapping[str, Any], main_transition: Mapping[str, Any]) -> float | None:
    crit_trans = main_transition.get("crit_trans", None)
    if isinstance(crit_trans, Mapping):
        Tc = _as_finite_float_or_none(crit_trans.get("Tcrit", None))
        if Tc is not None:
            return Tc
    false_key = main_transition.get("high_phase", None)
    true_key = main_transition.get("low_phase", None)
    for candidate in summary.get("critical_transitions", []):
        if candidate.get("high_phase") == false_key and candidate.get("low_phase") == true_key:
            return _as_finite_float_or_none(candidate.get("Tcrit", None))
    return None


def _extract_action_information(main_transition: Mapping[str, Any], Tn: float | None) -> tuple[float, float]:
    S_n = np.nan
    S_over_Tn = np.nan
    obs = main_transition.get("obs", None)
    if isinstance(obs, Mapping):
        S_n = _as_finite_float_or_none(obs.get("S", np.nan)) or np.nan
        S_over_Tn = _as_finite_float_or_none(obs.get("S_over_T", np.nan)) or np.nan
    if not np.isfinite(S_n):
        for key in ("action", "S3", "S_E", "S_action"):
            value = _as_finite_float_or_none(main_transition.get(key, None))
            if value is not None:
                S_n = value
                break
    if not np.isfinite(S_over_Tn) and Tn is not None and Tn > 0.0 and np.isfinite(S_n):
        S_over_Tn = S_n / Tn
    return float(S_n), float(S_over_Tn)


def compute_finite_temperature_summary(
    params: EFT68Parameters,
    *,
    T_min: float = 1.0,
    T_max: float = 250.0,
    phi_scan_range: tuple[float, float] | None = None,
    n_phi_scan: int = 1200,
    n_T_seeds: int = 3,
    deltaX_target: float = 0.1,
    x_eps: float = 1e-3,
    T_eps: float = 1e-2,
    deriv_order: int = 4,
    phitol: float = 1e-5,
    overlapAngle: float = 45.0,
    nuclCriterion: Callable[[float, float], float] | None = None,
    Tn_Ttol: float = 1e-3,
    Tn_maxiter: int = 100,
    include_zeroT_loops: bool = True,
    include_scalar_loops: bool = True,
    renormalize_zeroT_loops: bool = True,
    include_thermal: bool = True,
    include_scalar_thermal: bool = True,
    include_daisy: bool = False,
    thermal_approx: str = "spline",
    verbose: bool = True,
) -> dict[str, Any]:
    """Compute phases, critical transitions, nucleation temperature and spinodals."""
    if phi_scan_range is None:
        phi_scan_range = (0.0, params.f_GeV)
    problem = build_finite_temperature_problem(
        params,
        x_eps=x_eps,
        T_eps=T_eps,
        deriv_order=deriv_order,
        include_zeroT_loops=include_zeroT_loops,
        include_scalar_loops=include_scalar_loops,
        renormalize_zeroT_loops=renormalize_zeroT_loops,
        include_thermal=include_thermal,
        include_scalar_thermal=include_scalar_thermal,
        include_daisy=include_daisy,
        thermal_approx=thermal_approx,
    )
    nucleation_function = nuclCriterion or thesis_nucleation_criterion
    model_parameters = asdict(params)
    scan_parameters = {
        "T_min": T_min,
        "T_max": T_max,
        "phi_scan_range": phi_scan_range,
        "n_phi_scan": n_phi_scan,
        "n_T_seeds": n_T_seeds,
        "deltaX_target": deltaX_target,
        "x_eps": x_eps,
        "T_eps": T_eps,
        "deriv_order": deriv_order,
        "phitol": phitol,
        "overlapAngle": overlapAngle,
        "Tn_Ttol": Tn_Ttol,
        "Tn_maxiter": Tn_maxiter,
        "include_zeroT_loops": include_zeroT_loops,
        "include_scalar_loops": include_scalar_loops,
        "renormalize_zeroT_loops": renormalize_zeroT_loops,
        "include_thermal": include_thermal,
        "include_scalar_thermal": include_scalar_thermal,
        "include_daisy": include_daisy,
    }
    try:
        summary = _build_phases_and_transitions(
            problem["V_XT"],
            problem["dVdphi_XT"],
            problem["hessV_XT"],
            problem["dVdT_XT"],
            problem["dgradT_XT"],
            T_min=T_min,
            T_max=T_max,
            phi_range=phi_scan_range,
            n_phi_scan=n_phi_scan,
            n_T_seeds=n_T_seeds,
            deltaX_target=deltaX_target,
            tunnelFromPhase_args={
                "phitol": phitol,
                "overlapAngle": overlapAngle,
                "nuclCriterion": nucleation_function,
                "verbose": verbose,
                "fullTunneling_params": None,
            },
            nuclCriterion=nucleation_function,
            Tn_Ttol=Tn_Ttol,
            Tn_maxiter=Tn_maxiter,
            verbose=verbose,
        )
    except Exception as err:
        print("\n[Finite T warning] Phase tracing failed:", repr(err))
        summary = {
            **problem,
            "model_parameters": model_parameters,
            "scan_parameters": scan_parameters,
            "T_min": T_min,
            "T_max": T_max,
            "phi_range": phi_scan_range,
            "phases": {},
            "critical_transitions": [],
            "full_transitions": [],
            "main_transition": None,
            "key_temperatures": {},
            "transition_error": repr(err),
        }
        return summary

    summary.update(problem)
    summary["model_parameters"] = model_parameters
    summary["scan_parameters"] = scan_parameters
    summary["T_min"] = T_min
    summary["T_max"] = T_max
    summary["phi_range"] = phi_scan_range

    main_transition = summary.get("main_transition", None)
    if main_transition is None:
        summary["key_temperatures"] = {}
        print("\n[Finite T] No first-order transition with nucleation was found.")
        return summary

    phases = summary["phases"]
    false_key = main_transition["high_phase"]
    true_key = main_transition["low_phase"]
    false_phase = phases[false_key]
    true_phase = phases[true_key]
    Tn = _as_finite_float_or_none(main_transition.get("Tnuc", None))
    Tc = _extract_critical_temperature(summary, main_transition)
    spin_false = _spinodal_data_for_phase(false_phase, problem["hessV_XT"])
    spin_true = _spinodal_data_for_phase(true_phase, problem["hessV_XT"])
    spinodal_target = Tn if Tn is not None else Tc
    if spinodal_target is None:
        spinodal_target = 0.5 * (
            max(float(false_phase.T[0]), float(true_phase.T[0]))
            + min(float(false_phase.T[-1]), float(true_phase.T[-1]))
        )
    Ts_false = _closest_spinodal_to_T(spinodal_target, spin_false["T_spinodals"])
    Ts_true = _closest_spinodal_to_T(spinodal_target, spin_true["T_spinodals"])
    S_n, S_over_Tn = _extract_action_information(main_transition, Tn)
    phi_false_Tn = float(np.asarray(false_phase.valAt(Tn), dtype=float).ravel()[0]) if Tn else np.nan
    phi_true_Tn = float(np.asarray(true_phase.valAt(Tn), dtype=float).ravel()[0]) if Tn else np.nan
    summary["spinodal_false_phase"] = spin_false
    summary["spinodal_true_phase"] = spin_true
    summary["key_temperatures"] = {
        "Tn": Tn,
        "Tc": Tc,
        "Ts_false": Ts_false,
        "Ts_true": Ts_true,
        "S_at_Tn": S_n,
        "S_over_Tn": S_over_Tn,
        "false_phase_key": false_key,
        "true_phase_key": true_key,
        "phi_false_Tn": phi_false_Tn,
        "phi_true_Tn": phi_true_Tn,
        "vTn_over_Tn": phi_true_Tn / Tn if Tn and Tn > 0.0 else np.nan,
    }
    print("\n" + "=" * 76)
    print("Finite-temperature transition summary")
    print("=" * 76)
    print(f" T_n        : {Tn if Tn is not None else np.nan:10.5g} GeV")
    print(f" T_c        : {Tc if Tc is not None else np.nan:10.5g} GeV")
    print(f" phi_true/Tn: {summary['key_temperatures']['vTn_over_Tn']:10.5g}")
    print(f" S3/Tn      : {S_over_Tn:10.5g}")
    print("=" * 76)
    return summary



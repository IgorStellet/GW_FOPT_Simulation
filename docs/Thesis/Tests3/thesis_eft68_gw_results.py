def _extract_bounce_inputs_from_summary(summary: Mapping[str, Any]) -> dict[str, float]:
    main = summary.get("main_transition", None)
    key_T = summary.get("key_temperatures", {}) or {}
    if main is None:
        raise RuntimeError("No main transition was found.")
    Tn = _as_finite_float_or_none(key_T.get("Tn"))
    if Tn is None:
        raise RuntimeError("No finite Tn was found.")
    phases = summary["phases"]
    false_phase = phases[main["high_phase"]]
    true_phase = phases[main["low_phase"]]
    return {
        "Tn": Tn,
        "phi_false": float(np.asarray(false_phase.valAt(Tn), dtype=float).ravel()[0]),
        "phi_true": float(np.asarray(true_phase.valAt(Tn), dtype=float).ravel()[0]),
    }


def _make_phi_only_potential_at_Tn(summary: Mapping[str, Any], *, Tn: float) -> Callable[[ArrayLike | float], np.ndarray]:
    params = EFT68Parameters(**summary["model_parameters"])
    scan = summary.get("scan_parameters", {}) or {}
    def V_phi(phi: ArrayLike | float) -> np.ndarray:
        return np.asarray(
            V_effective_potential(
                phi,
                Tn,
                params,
                include_zeroT_loops=bool(scan.get("include_zeroT_loops", True)),
                include_scalar_loops=bool(scan.get("include_scalar_loops", True)),
                renormalize_zeroT_loops=bool(scan.get("renormalize_zeroT_loops", True)),
                include_thermal=True,
                include_scalar_thermal=bool(scan.get("include_scalar_thermal", True)),
                include_daisy=bool(scan.get("include_daisy", False)),
            ),
            dtype=float,
        )
    return V_phi


def build_bounce_solution_at_Tn(summary: dict[str, Any], *, phitol: float = 1e-5, thinCutoff: float = 1e-4, npoints: int = 800, xguess: float | None = None) -> dict[str, Any]:
    inputs = _extract_bounce_inputs_from_summary(summary)
    V_phi = _make_phi_only_potential_at_Tn(summary, Tn=inputs["Tn"])
    inst = SingleFieldInstanton(phi_absMin=inputs["phi_true"], phi_metaMin=inputs["phi_false"], V=V_phi, alpha=2, phi_eps=1e-3)
    profile = compute_profile(inst, xguess=xguess, phitol=phitol, thinCutoff=thinCutoff, npoints=npoints)
    br = inst.actionBreakdown(profile)
    bounce = {**inputs, "V_phi": V_phi, "instanton": inst, "profile": profile, "action_breakdown": br, "S3": float(br.S_total), "S3_over_T": float(br.S_total) / inputs["Tn"]}
    summary["bounce"] = bounce
    print("\nBounce at Tn: S3/Tn =", bounce["S3_over_T"])
    return bounce


def _profile_arrays(profile: Any) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    r = np.asarray(profile.R, dtype=float)
    phi = np.asarray(profile.Phi, dtype=float)
    dphi = np.asarray(profile.dPhi, dtype=float) if hasattr(profile, "dPhi") else np.gradient(phi, r, edge_order=2)
    return r, phi, dphi


def _get_bounce(summary: Mapping[str, Any]) -> dict[str, Any]:
    if "bounce" not in summary:
        raise RuntimeError("Bounce has not been computed yet.")
    return summary["bounce"]



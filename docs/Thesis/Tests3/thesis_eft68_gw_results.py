@dataclass(frozen=True)
class EFT68Parameters:
    """Physics inputs for the dimension-six/eight EFT potential."""

    c6_over_f2_TeV2: float = 2.0
    c8_over_f4_TeV4: float = 2.0
    f_GeV: float = 1000.0
    m_h: float = DEFAULT_MH
    v: float = DEFAULT_VEV
    m_w: float = DEFAULT_MW
    m_z: float = DEFAULT_MZ
    m_t: float = DEFAULT_MT
    # Optional Gláuber functional-measure deformation.
    use_glauber_measure: bool = False
    C_glauber: float = 0.0
    Lambda_glauber: float = 1000.0

    @property
    def k6_GeV2(self) -> float:
        """Return c6/f^2 in GeV^-2 from the TeV^-2 input."""
        return float(self.c6_over_f2_TeV2) * 1.0e-6

    @property
    def k8_GeV4(self) -> float:
        """Return c8/f^4 in GeV^-4 from the TeV^-4 input."""
        return float(self.c8_over_f4_TeV4) * 1.0e-12

    @property
    def y_t(self) -> float:
        return np.sqrt(2.0) * self.m_t / self.v

    @property
    def g(self) -> float:
        return 2.0 * self.m_w / self.v

    @property
    def gp(self) -> float:
        return np.sqrt(max(4.0 * self.m_z**2 / self.v**2 - self.g**2, 0.0))

    @property
    def lambda_tree(self) -> float:
        """Tree-level lambda fixed by V''(v)=m_h^2."""
        v = self.v
        return (
            self.m_h**2 / (2.0 * v**2)
            - 1.5 * self.k6_GeV2 * v**2
            - 1.5 * self.k8_GeV4 * v**4
        )

    @property
    def mu2_tree(self) -> float:
        """Tree-level mu^2 fixed by V'(v)=0."""
        v = self.v
        return (
            self.lambda_tree * v**2
            + 0.75 * self.k6_GeV2 * v**4
            + 0.5 * self.k8_GeV4 * v**6
        )

    @property
    def a0_mean_field(self) -> float:
        """High-T mean-field thermal mass coefficient."""
        v = self.v
        return (
            4.0 * self.m_h**2 / v**2
            + 3.0 * self.g**2
            + self.gp**2
            + 4.0 * self.y_t**2
            - 12.0 * self.k6_GeV2 * v**2
            - 12.0 * self.k8_GeV4 * v**4
        ) / 16.0

    @property
    def physicality_upper_c6_TeV2(self) -> float:
        """Eq. (2.14)-like upper bound on c6/f^2 in TeV^-2."""
        v = self.v
        upper_GeV2 = self.m_h**2 / v**4 - 1.5 * v**2 * self.k8_GeV4
        return upper_GeV2 * 1.0e6

    @property
    def combo_TeV2(self) -> float:
        """Combination 2 c6/f^2 + 3 v^2 c8/f^4 in TeV^-2 units."""
        return 2.0 * self.c6_over_f2_TeV2 + 3.0 * (self.v / 1000.0) ** 2 * self.c8_over_f4_TeV4


